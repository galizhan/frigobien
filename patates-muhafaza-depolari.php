					<div class="col-md-12">
						<p class='resimyanyazisi'>
<strong>Patates muhafaza depoları</strong>nda kaliteli patates saklayabilmek, iyi yapı tasarımı, özenle tasarlanmış hava dağıtımı, güvenilir bir havalandırma sistemi ve depoyu gözeten insan, faktöründen geçer.<br>Bunların her biri, bir zincirin halkaları gibidir. Nasıl ki bir zincir en zayıf halkası kadar sağlam ise, bu unsurların birinde oluşabilecek olası bir aksaklık da, diğerleri tarafından giderilemez, dolayısıyla istenilen depolama amacına ulaşılamaz. Patates yumruları yaşayan, soluyan organizmalardır. Solurken karbondioksit, nem ve ısı verirler.<p>					</div>
					<div class="clearfix"></div>
					<div class="col-md-12">
						
<h2>Patates Hasat</h2>
<p>Patateste hasat zamanının geldiği yaprak ve sapların sararıp kuruduğu, yumruların normal büyüklüğünü alarak bitkiden kolayca ayrıldığı ve kabuğun kalınlaşıp sertleştiğinden anlaşılır. Kabuk tırnakla kolayca soyulmaz. Yumrunun kesiti ıslak değil, koyu bir görünümdedir.
Patatesin hasadında çok dikkatli olmak gerekir. Yumrular kesilip zedelenmemeli, toprakta yumru bırakılmamalıdır. Söküm sırasında toprak yaş olmamalı, tavında bulunmalıdır.</p>
<p>Patates hasadı değişik şekillerde yapılabilir:</p>
<ul>
	<li>Bel, çatal bel ya da çapa ile patatesler sökülüp el ile toplanır.</li>
	<li>Pullukla sökülüp el ile bitkiden ayrılarak toplanır.</li>
	<li>Tek veya çift sırayı söküp bitkiden ayıran makineler ile de hasat yapılır.</li>
</ul>
<p>Hasattan sonra yumrular ıslak ise gölgede kurutulur. Hasta, çürük, berelenmiş ve kabuğu soyulmuş olanları ayıklanır. Sonra iri, orta ve küçük boy olmak üzere sınıflandırılarak file çuvallara doldurulur.</p>
<h2>Patates Depolama</h2>
<p>Karbondioksit, nem ve ısının son derece yüksek düzeylere çıkmasını önlemek ve yaşayan canlı patates yumruları için uygun ortam sağlamak amacıyla patates yumruları havalandırılır. Patates deposunun havalandırılması ve soğutulması, deponun ve yumruların her tarafında doğru hava sıcaklığı, hava sirkülasyonu ve dağılımının sağlanması olarak tanımlanabilir. Bu üç faktörün amacı, depo içindeki patateslerin her yerinde düzgün yumru sıcaklığı, nem ve havanın elde edilmesidir. Patatesler bu çevre koşullarının sağlanmasıyla, çeşit özelliklerine bağlı olarak, yüksek kalitede 10 – 12 aylık uzun süreli depolanabilir.</p>
<p>Patatesler, gerek yığın halinde (net depolama kapasitesi veya yığın yoğunluğu: 600 ile 650 kg/m3) gerekse de kenevir veya naylon çuvallarda (her çuval 50 kg, yığma yoğunluğu: 500 kg/m3) depolanır. Mekanik yükleme ve boşaltma gerektiren geleneksel depolama yöntemi (çuvalarda
depolama) ise, daha düşük kapasiteler için ve çoğu müşterinin aynı depoyu kullanması durumunda tercih edilir. Ancak, patateslerin depolanması için hangi tip yapı kullanılırsa kullanılsın, emniyetli, verimli ve ekonomik patates deposu için, aşağıda belirtilen bazı özellikler yerine getirilmelidir.</p>
<ul>
	<li>Yapısal olarak sağlam olmalıdır. Depodaki patateslerin basıncı gibi, dış kuvvetlerden (rüzgar, kar, yağmur) ortaya çıkan yük basınçlarına karşı dayanıklı olmalıdır,</li>
	<li>Yeterli izolasyonu olmalıdır. Yeterli izolasyonun sağlanmasıyla, depo içinde yoğuşma oluşumuna önlem alınır. Toplam ısı geçirgenlik katsayısı 0,284 W/rmK olmalıdır. Bu miktar izolasyon, yaklaşık olarak 75-100mm kalınlığındaki poliüretana ve 150-200mm kalınlığındaki fiberglasa eşdeğerdir.
Patates deposunun tavanındaki yoğuşma, deponun her tarafında yeterli izolasyonun sağlanması yanında, aşağıdakilerden bir veya birkaçını uygulayarak azaltılabilir veya yok edilebilir.</li>
	<ul>
		<li>Dönüş hava kanalı, tavanın en üst kısmına kadar uzatılır,</li>
		<li>Havanın by-pass edilmesi veya yardımcı fanlar kullanılmasıyla, tavan boyunca dolaşan sabit hava akışı sağlanır,</li>
		<li>Gerektiğinde, su tutma kapasitesini artırmak için, patates üzerinde tavan boyunca dolaşan havanın sıcaklığı 1-2 T (0,55 – 1,10°C) arttırılır.</li>
	</ul>
	<li>Dışarıdan su geçirmez olmalıdır, Deponun çatı malzemesi,- yağmur veya nemin izolasyon malzesine girmesini önlemelidir. Bu, özellikle cam yünü gibi izolasyon malzemelerinde önemlidir. Islanmış izolasyon; ısının, tavan ve duvarlar arasından kaçmasına neden olan kötü bir izolasyon maddesidir,</li>
	<li>İç kısımdan buhar geçirmez olmalıdır. İzolasyon maddesinin dıştaki neme karşı korunması yanında, iç kısımdan gelen neme karşı da korunmalıdır. Soluyan patateslerden ortaya çıkan nemin izolasyon maddesine girmesini önlemek için, iç kısım nem yalıtım maddesiyle kaplanmalıdır. Bunun için polietilen plastik, alüminyum folyo veya uygun başka bir malzeme kullanılabilir.</li>
