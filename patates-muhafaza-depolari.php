				<h2>Patates Muhafaza Depoları</h2>
				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/patates-muhafaza-depolari.webp" alt="Frigobien Patates Muhafaza Depoları" title="Frigobien Patates Muhafaza Depoları" />
