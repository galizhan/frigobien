					<div class="col-md-12">
						<strong>Yumurta soğuk hava depoları</strong>, <strong>Frigobien</strong>'in her türlü tarımsal ve hayvansal gıdada olduğu gibi profesyonel olarak çözüm ürettiği faaliyet alanlarından biridir.
<p>Ülkemiz de ve birçok ülkede <u>yumurta yıkanmaz ve yağlanmaz</u>. Kümesten toplanan yumurtanın, tüketime sunuluncaya kadar, hangi çevre şartlarında muhafaza edileceğine dair bir zorunluluk ve yaygın bir uygulama da bulunmamaktadır. Birçok ülkede yumurta toplandıktan sonra yıkanır; bazı ülkelerde yağlanır sonra sıcaklık ve nispi nem kontrollü odalarda depolanır.</p>
<p>Uygun olmayan depolama şartlarında <strong>muhafaza edilen yumurta</strong>, kısa süre içinde bozularak sağlık için zararlı hale gelir. <strong>Yumurtanın dayanıklılık süresini uzatma</strong>ya yönelik konservasyon metotları büyük önem taşır. Yumurta, kendi doğal yapısı ve bileşimi nedeni ile mikrobiyolojik bozulmalara karşı az da olsa korunabilmektedir. Yine de yumurta çabuk bozulabilen bir gıda maddesidir. Gerekli önlemler alınmadığında, toplama ile tüketim arasında geçen sürede yumurta kalitesinde önemli kayıplar olur. Yumurtanın kalitesinin korunması için alınabilecek tüm önlemler yumurtadaki gelişmeleri durdurmaz, sadece geciktirebilir. Yumurtanın, oluşumundan tüketimine kadar geçen süre içinde çeşitli çevre faktörlerinin etkisi altında kalması sonucu besin değerinde önemli ölçüde azalmalar olur. Bu nedenle yumurtanın tazelik durumunun korunması, besin hijyeni açısından önem taşır. Yumurtlama tarihinden itibaren yumurtada, biyolojik, kimyasal ve fiziksel değişimler meydana gelir.</p>
<p>Uygun olmayan depolama şartlarında tutulan yumurta, kısa sürede bozularak sağlık açısından sakıncalı hale gelir. Bu nedenle yumurtanın dayanma süresini uzatmaya yönelik yöntemler önem taşımaktadır.</p>
<p>Bu yöntemlerin amaçları şu başlıklar altında toplanabilir:</p>
<ul>
<li>Yumurta kabuğuna bakteri bulaşmasını önlemek</li>
<li>Bakterilerin yayılmasını önlemek</li>
<li>Bakteri gelişmesini geciktirmek</li>
<li>Raf ömrünü uzatmak</li>
</ul>
<p><strong>Yumurta muhafazası</strong>nda en yaygın olarak kullanılan yöntem soğutmadır. Yumurta, en iyi şekilde uygun <strong>soğuk depo</strong>larda muhafaza edilebilir. Buzdolabında bir hafta bekletilmiş yumurta, oda ısısında bir gün bekletilmiş yumurtadan daha tazedir. Soğuk depolara konulacak yumurta, yeni ambalajlara yerleştirilmiş taze, temiz ve kuru olmalıdır. Çatlak ve kırık yumurtalar depolanmamalıdır. Depolara yerleştirilen yumurta ambalajları arasında hava sirkülasyonunu sağlayacak yeterli aralık bırakılmalı; ancak hava akımı doğrudan ambalaj üzerine gelmemelidir.</p>
<p>Depolama süresini uzatmak ve kalite kaybını en az düzeye indirmek için <em>soğuk depoların sıcaklığı -1°C ile -2°C arasında, nispi rutubeti ise en az %90 olmalıdır.</em> <strong>Uygun şartlarda soğuk depolarda yumurtalar 6–7 aya kadar muhafaza edilebilir</strong>. <em>Ticari olarak 6 ay veya daha uzun süre saklanacak kabuklu yumurtalar için -1ºC ile 0ºC ve %70–80 rölatif rutubet önerilmektedir.</em></p>
<p><strong>Soğuk depolama</strong>da kalite kaybını asgaride tutmak ve muhafaza süresini uzatmak için yumurta kabukları yağla kaplanarak depolanır. Bunun için renksiz ve kokusuz madeni yağlar kullanılır. Temiz ve kuru yumurta bu yağa batırılarak veya yumurta üzerine püskürtülerek kabuk yağla kaplanır; ancak bu tür bir uygulamanın mikrobiyolojik açıdan bir avantaj sağlamadığı da bildirilmektedir. Kullanılacak yağın sıcaklığı; yumurtanın sıcaklığından birkaç derece yüksek olmalıdır. Yumurta, termostabilizasyon 60°C sıcaklıktaki suya veya yağa 15 dakika daldırılarak bir nevi pastörizasyon uygulanır. Daha sonra soğutularak muhafaza depolarına alınır. Depoda, aşırı rutubetten meydana gelecek küf gelişmesini önlemek için uygun havalandırma tertibatı bulunmalıdır. Depolama sırasında arzu edilen rölatif nemin korunabilmesi için depo içerisinde hava sirkülasyonunun sağlanması önemlidir. Yumurtanın soğuk depolardan çıkarıldıktan sonra ortam sıcaklığında bekletilmesi halinde kabuk üzerinde biriken kondense su mikroorganizmaların süratle üremesine neden olacağından soğuk zincirin tüketiciye ulaşıncaya kadar korunması gerekir. Saklama koşullarında; atmosfere ozon ve CO<sub>2</sub> ilave edilerek yumurtanın raf ömrü uzatılır.</p>
<p>Ev şartlarında yumurta buzdolabında (0–5°C), çabuk bozulmasını önlemek için alınan orijinal karton kutusu içerisinde, yıkanmadan saklanmalıdır. Bu nedenle yumurta, kullanılacağı zaman yıkanmalıdır.</p>					</div>
