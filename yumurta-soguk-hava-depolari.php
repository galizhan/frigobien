				<h2>Yumurta Soğuk Hava Depoları</h2>
				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/yumurta-soguk-hava-depolari.webp" alt="Frigobien Yumurta Soğuk Hava Depoları" title="Frigobien Yumurta Soğuk Hava Depoları" />
