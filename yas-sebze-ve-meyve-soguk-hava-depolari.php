						<p><strong>Yaş sebze ve meyve soğuk hava depoları</strong>, her ürüne göre ayrı tasarlanmalı ve ihtiyaca göre soğutulmalıdır. Çünkü her ürün aynı şartlarda depolanmaz.</p>
<p>Dünya konjonktüründe üretim/tüketim ilişkisine bakıldığında, hiçbir ana ürün, üretildiği anda tüketilemez. Özellikle, sözü edilen önemli meyve/sebzeler hasat sonrası tüketilebilmeleri için bir zamana ve mekâna ihtiyaç vardır. Tüm üretilen gıdaların; kalite kaybına neden olmadan, bu gezegendeki herhangi bir ülkede yaşayan insanların beslenmesine sunmak, ancak muhafaza yöntemlerinden birine başvurmak ile olasıdır.</p>
<p>Dünyada ve ülkemizde özellikle taze meyve ve sebzelerde %50’lilere varan kayıplar olmaktadır. Örneğin ülkemizde büyük emeklerle ürettiğimiz meyvelerin %25’ini hasattan tüketici sofrasına getirinceye kadar kaybetmekteyiz. Bir başka deyişle; ortalama 3 milyon ton ürettiğimiz elmamızın 750.000 tonunu çöpe atmaktayız. Nedenlerin %44’ü, üretimden tüketiciye ulaşıncaya kadar olan aşamadaki uygunsuz koşullardan kaynaklanmaktadır.</p>
<p>Dünya geneline bakıldığında ise kayıp oranları %20 civarında olup sadece elmadaki kayıp 15 milyon ton civarındadır. Bu miktar da tüm Afrika ülkelerinin bir yıllık gıda gereksinimidir. İşte bu nedenlerden dolayı, gıda israfını azaltmak, taze meyve-sebze kayıplarını en aza indirmek için; hasat sonrası işlemlerin doğru yapılması, özellikle soğuk muhafaza tesislerinin ürün özelliklerine uygun dizayn edilmesi, soğutma yükleri ve malzeme seçimlerinin işlevlerine uygun seçilmesi büyük önem arz etmektedir. Frigobien sizlere işinde uzmanlaşmış ekibi ile proje önerileri çalışmakta ve en uygun soğuk hava deposu çözümlerini sunmaktadır.</p>
<h2>Bazı meyve ve sebzelerin soğuk muhafaza depolarında muhafazası</h2>
<p><strong>Elma:</strong> Meyveler yeme olumuna gelmeden hasat olumunda toplanır. İri meyveler depolamaya dayanıksız olduklarından, küçük meyvelerde ekonomik olmadıklarından ayrılarak 55-75mm. çapında olanlar depolanmalıdır. Ayrıca geçici çeşitler depolamaya daha uygundur.</p>
<p><strong>Armut:</strong> Birçok armut çeşitlerinde elmanın aksine depoda olgunluk ilerlemediğinden depolamadan önce olgunlaştırılması gerekir.</p>
<p><strong>Limon:</strong> Limon uzun süre başarılı Resimde depolanan meyve türlerindendir. Ancak limonun dış yüzeyinde mekanik zararlanmalardan kaçınılmalıdır. Zira sağlıklı meyvelerde dahi küf mantarları etkili olduğundan mücadele yapılması zorunlu görülmektedir.</p>
<p><strong>Portakal:</strong> Portakal depolaması için mumlama ve kabukta küf gelişmesi için önlem alınması zorunlu görülmektedir.</p>
<p><strong>Üzüm:</strong> Üzüm hasattan sonra olgunlaşmayan meyvelerden olduğu için yeme olumunda hasat edilerek depolanır. Ayrıca çürümeyi önlemek için kükürtlenmesi gerekir.</p>
<p><strong>Soğan:</strong> Depolanmadan önce soğanların iyi bir şekilde kurutulması gerekmektedir. Soğanın havalandırma ihtiyacı fazla olup yüksek nem varlığında ve havalandırmanın yetersiz olması halinde küf tehlikesi mevcuttur. Ayrıca depolama sıcaklığı 0°C olup donma tehlikesi nedeniyle -1°C nin altına düşmemesi gerekir.</p>					</div>
