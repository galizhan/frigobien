						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/yas-sebze-ve-meyve-soguk-hava-depolari.webp" alt="Frigobien Yaş Sebze ve Meyve Soğuk Hava Depoları" title="Frigobien Yaş Sebze ve Meyve Soğuk Hava Depoları" />
