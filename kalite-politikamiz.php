                <h2>Kalite Politikamız</h2>
                <div>
					<p>Frigobien,</p>
					<ul>
						<li>Tüm süreçlerde, müşterinin ihtiyaç ve beklentilerini tam olarak karşılayan bir anlayış içinde güvenilir ve aranan bir firma olmayı,</li>
						<li>Maliyetleri düşürmek ve karlılığı arttırmak için teknolojik gelişmeleri yakından takip etmeyi ve uygulamayı,</li>
						<li>Kalite sistemleri çerçevesinde tüm çalışanların daha yetkin ve yeteneklerini en üst seviyede kullanabilen kişiler haline gelmeleri için ekip çalışmasına önem vererek kalite düzeyini sürekli yükseltmeyi,</li>
						<li>Tüm çalışanlarda kalite bilincini tesis etmeyi,</li>
						<li>Tedarikçiler ile güvene dayalı bir işbirliği içinde olmayı,</li>
						<li>Firmamızın ve Türkiye’nin adını dünyaya daha çok duyurmak amacına hizmet etmek üzere, mevcut teknik ve idari birikimlerimizi yurt dışına taşımayı,</li>
						<li>En uygun, en doğru ve en ekonomik çözümleri en hızlı biçimde üreterek daha sonra ortaya çıkabilecek uygunsuzlukların önüne geçmeyi,</li>
						<li>Başlanan her projeyi zamanında,en üst düzey tekniklerle projesine tam uyumlu, istenen kalitede ve bütçe sınırları içinde eksiksiz tamamlamayı,</li>
						<li>İçinde bulunduğumuz topluma ve çevreye saygılı, örnek bir kuruluş olmayı, ve iş hacmini sürekli geliştirerek, ülke ekonomisine katkıda bulunmayı</li>
						<p>kalite politikası olarak belirlemiştir.</p>
					</ul>
