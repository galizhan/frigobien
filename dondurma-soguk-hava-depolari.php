					<div class="col-md-12">
						<p><strong>Dondurma soğuk hava depoları</strong>, dondurma üretiminde olmazsa olmaz bir aşamadır ve bu aşamada Frigobien en son teknoloji ile işletmelere çözümler üretmektedir.</p>
<p>Endüstriyel dondurma üretim aşamaları aşağıdaki gibidir.</p>
<ul>
<li>Ham madde Girdileri</li>
<li>Ham Maddelerin Tartımı ve Hazırlanması</li>
<li>Karıştırma ve Ön Isıtma</li>
<li>Homojenizasyon</li>
<li>Pastörizasyon</li>
<li><strong>Soğutma</strong></li>
<li>Aroma ve Renk Maddelerinin Katılması</li>
<li>Olgunlaştırma</li>
<li>Dondurma</li>
<li>Dolum ve Ambalajlama</li>
<li>Paketleme</li>
<li>Sertleştirme</li>
<li><strong>Depolama</strong></li>
<li>Satış</li>
</ul>
<p>Dondurma süt ve ürünleri, tatlandırıcı maddeler, stabilizer-emülsifiyerler, renk, aroma ve çeşni maddelerinden oluşan karışımdır. Dondurma bileşimine giren maddelerin henüz dondurulmamış haldeki karışımına dondurma miksi denir. Dondurmanın yapısını hava kabarcıkları, buz kristalleri, yağ globülleri, şeker, protein ve stabilizerler oluşturur.</p>
<p>Günümüzde dondurma sektörü, gıda endüstrisi ve süt teknolojisi alanında en hızlı gelişen ve gün geçtikçe önemi artan dallardan biridir. Ülkemizde dondurma sektörünün hızla büyümesinin en önemli nedeni ham maddenin bol, ucuz ve çeşitli olmasıdır. Yine süt içmeyi çok sevmeyen insanımızın yaz aylarında dondurmaya aşırı ilgi göstermesi, bu sektörün gelişmesini desteklemiştir.</p>
<h2>Soğutma</h2>
<p>Homojenizasyon/pastörizasyon işleminden sonra miks, 0-5°C’ye soğutularak işleninceye kadar olgunlaştırma tankında bekletilir. Mikste viskozite artışını önlemek ve işlenmesini kolaylaştırmak bakımından kısa sürede soğutma önemlidir. Soğutmayla yağın kristalizasyon derecesi artar, dondurucunun kapasitesi artar, bakteri faaliyeti de engellenir. Diğer firmalara satılacak olan mikslerin depolama ömrü uzar. Dondurulma işleminden önce miksin en fazla 5°C’ye kadar soğutularak bekletilmesi gerekir. Olgunlaştırma süresi kullanılan stabilizere göre 3-6 saat ile 24 saat arasında değişir. Olgunlaştırmayla miksin yapısında değişimler meydana gelir ve bunlar ürünün yapı ve tekstürünü etkiler. Olgunlaştırma sonucunda dondurmanın yapısı düzelir, kremamsı his artar, erimeye karşı dayanım artar ve depolama nitelikleri iyileşir. Soğutmayla başlayan yağ globüllerindeki kristalizasyon olgunlaştırma süresince ilerler. Dondurulma işlemi üretimin en önemli aşamasıdır ve son ürünün kalitesini, hoşa giderliğini ve verimliliğini direkt olarak etkiler. İki kısımdan oluşur.</p>
<ul>
<li>Miks sürekli olarak karıştırılırken ortama hava verilerek hızlı bir şekilde dondurulma (belirli büyüklüklerde buz kristalleri oluşturulması).</li>
<li>Kısmi donmuş karıştırılma olmaksızın ısının hızlı bir şekilde uzaklaştırılmasıyla sertleştirme bu aşamada buz kristallerinin yüzeyinde bulunan su donar.</li>
</ul>
<h2>Sertleştirme</h2>
<p>Dondurma dondurucudan çıktığında yarı-katı kıvamdadır ve şeklini muhafaza edemeyebilir. Dondurulma işlemi paketin içinde sıcaklık - 18°C (-25°C/-30°C) ’ye düşünceye kadar devam ettirilir. Büyük buz kristallerinin oluşmaması için sertleştirme hızlı olmalıdır. Paketin ortasındaki sıcaklık -18°C’ye düşünceye kadarki süre kriter
kabul edilir (200 ml’lik paketler için 30 dakika). Sertleştirmeden önce paketlerin üst üste yığılması deformasyona neden olur, hacim artışı azalır ve yüzeyde renk değişimi görülür çünkü hava dışarı çıkmaktadır.
<h2>Sertleştirme Yöntemleri</h2>
<p>Küçük işletmelerde kabinler, büyük işletmelerde sertleştirme tünelleri direkt hat veya spiral şekilde dizayn edilebilir. Depolama odaları içinde soğuk hücrelerin veya bölgelerin kullanılması (paketler depolara doğru taşınırken bu bölgelerde üzerlerine soğuk hava üflenir). Soğuk hücreler sertleştirme tünellerinde de yer alabilir.
<h2>Depolama</h2>
