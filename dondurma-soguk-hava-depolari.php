				<h2>Dondurma Soğuk Hava Depoları</h2>
				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/dondurma-soguk-hava-depolari.webp" alt="Frigobien Dondurma Soğuk Hava Depoları" title="Frigobien Dondurma Soğuk Hava Depoları" />
