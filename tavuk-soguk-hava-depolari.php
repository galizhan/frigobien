						<p><strong>Tavuk soğuk hava deposu</strong>, <strong>Frigobien</strong>'in her türlü tarımsal ve hayvansal gıdada olduğu gibi profesyonel olarak çözüm ürettiği faaliyet alanlarından biridir.</p>
<p>Tavuk eti ve ürünleri mikroorganizmaların gelişip çoğalabilmeleri için uygun ortamlardır. Bu tür gıdalar, yüksek nem içerikleri, azotlu besin öğeleri mineral ve diğer gelişme faktörlerince zengin olmalarının yanında belli oranda fermente olabilen karbonhidrat (glikojen) içermeleri ve pH değerlerinin birçok mikroorganizmanın gelişmesine elverişli olması nedeniyle mikrobiyal gelişme sonucu kolayca bozulma niteliği taşımaktadırlar. Normal koşullarda, sağlıklı bir tavuk etinin iç dokusunda pek az sayıda mikroorganizma bulunabilir veya hiç mikroorganizma bulunmaz. Ancak, tavuk etleri kesim, yüzme, parçalama, taşıma, depolama ve işleme sırasında önemli ölçüde dış kaynaklı mikrobiyal kontaminasyona maruz kalırlar. Özellikle mikroorganizma bulaşmaları, kesim sonrası uygulanan işlemlerin temiz ve doğru olmamasından kaynaklanmaktadır.</p>
<p>Kanatlı hayvanların kesim işleminde yer alan soğutma aşaması, mikroorganizmaların gelişimini ve et kalitesini etkileyen en önemli basamaklardan birisidir. Soğutma işlemi ile karkas sıcaklıklarının düşürülmesi biyokimyasal reaksiyonları geciktirmekte, mikroorganizmaların gelişimini engelleyerek etin raf ömrünün uzamasını sağlamaktadır. Karkas soğutma işleminin, kanatlı etinin görünümünü, su tutma kapasitesini, lezzetini, yumuşaklığını ve raf ömrünü etkileyen kesim sonrası biyokimyasal reaksiyonların gelişimi üzerine etkili olduğu belirtilmiştir.</p>
<p>Kesim işlemi sırasında, karkasta mikrobiyal gelişimin engellenebilmesi etin hızlı bir şekilde soğutulmasına bağlıdır. Karkas sıcaklığının, iç organ çıkarma işlemini takiben 4 saat içinde 4.4°C'ye düşürülmesi gerekmektedir. Karkasların soğutulma süresi, soğutma sistemlerinin kritik parametrelerinden biri olmakla birlikte, buna paralel olarak karkas sıcaklığının hızlı ve kısa zamanda düşürülmesi de etin kalite parametrelerinden biri olan mikroorganizmaların gelişimini etkilemektedir. Kesimhanelerde etlik piliçlerin kesilmesi sırasında karkasların soğutulması amacıyla su ve hava soğutma yöntemleri yaygın olarak kullanılmaktadır. Bunlara ek olarak evaporatif soğutma yöntemi de etkin soğutma yöntemleri arasında yer almaktadır. Su soğutma yöntemiyle karkas sıcaklığının düşürülmesi Amerika Birleşik Devletlerinde yaygın olarak kullanılırken, Avrupa, Kanada ve ülkemizde soğuk hava dolaşımlı soğutma yöntemi uygulanmaktadır.</p>
<p>Kanatlı karkaslarını soğutma aşamasında soğutma yöntemine bakılmaksızın öncelikli olarak ürünün sağlık güvenliği ve marketteki raf ömrü için mikrobiyal gelişmeye önem verilmektedir. Frigobien'de soğutma yöntemlerinin etin kalitesi için daha etkili, daha ekonomik, mikrobiyal gelişmeyi kontrol altına alan ve ürünün kalitesini azaltmadan değişen sıcaklığa uyum sağlatan sistemler geliştirilmektedir. İşletmeler kısa zamanlı soğutma, sağlıklı, minimum ağırlık kaybı, düşük enerji harcayan ve az masraflı soğutma yöntemini tercih etmektedir. Biz de bunu en iyi bilen olarak sizlere en iyisini sunmaya hazırız.</p>
