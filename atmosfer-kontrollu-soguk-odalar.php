				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/atmosfer-kontrollu-soguk-odalar.webp" alt="Frigobien Atmosfer Kontrollü Soğuk Odalar" title="Frigobien Atmosfer Kontrollü Soğuk Odalar" />
