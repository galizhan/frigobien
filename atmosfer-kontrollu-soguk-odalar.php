<div class='incelt'>
<p><strong>Atmosfer kontrollü soğuk odalar</strong>da amaç, uzun süre saklanması planlanan meyve ve sebzelerin karbondioksit(CO<sub>2</sub>) ve etilen(C<sub>2</sub>H<sub>4</sub>) gibi, ürünlerden salgılanan ve ürünlerin bozulma sürecini hızlandıran gazların emilmesini, oksijen(O<sub>2</sub>) oranının neredeyse sıfıra düşürülmesini ve azot(N) oranının artmasını sağlamaktır. Bu işlemlerin dışında <strong>atmosfer kontrollü soğuk odalar</strong>ın normal soğuk odalardan ve ya soğuk hava deposundan hiçbir farkı yoktur. Söz konusu işlemlerin sağlanabilmesi için de <strong>karbondioksit tutucu</strong>, <strong>etilen dönüştürücü</strong>, <strong>oksijen emiciler</strong> ve <strong>azot jeneratörü</strong> gereklidir.</p>
<h2 class='mt15 mb15 solayasla'>Ekipmanların Tanımları</h2>
<h3 class='solayasla'>Karbondioksit Tutucu</h3>
<p>Karbondioksit tutucusunda bulunan aktif kömür karbondioksit moleküllerini emer. Aktif kömürden geçirilen hava karbondioksitten arındırıldıktan sonra tekrar soğuk hava deposuna gönderilir. Böylece karbondioksit soğuk hava deposundan etkin şekilde uzaklaştırılmış olur.</p>
<h3 class='solayasla'>Etilen Dönüştürücü</h3>
<p>Etilen dönüştürücü, her birinde bir ısı depolayıcısı, bir katalitik dönüştürücüsü, ısıtma elemanları ve bir vantilatör bulunan iki sütundan oluşur. Her bir sütunda bulunan soğuk depo havası, dönüşümlü olarak yukarı doğru yönlendirilir ve ısıtılır. Isınan hava, etilen gazının parçalandığı katalitik dönüştürücüye gönderilir. Son olarak ikinci sütundaki katalitik dönüştürücüye gönderilen hava, içerisinde az miktarda kalan etilenden de ayrıştırılarak tekrar soğutularak soğuk hava deposuna zerk edilir. Böylece ortamdaki etilen miktarı, kimyasal madde kullanılmaksızın, milyarda bir (0,0000001) düzeyine indirilebilir, bu nedenle yapılan işlem çevreyle dosttur.</p>
<h3 class='solayasla'>Oksijen Emiciler</h3>
<p>Ortama oksijen girişi olduğu durumlarda (kapı açılması vb.) oksijen seviyesini düşürerek atmosfer kontrollü ortamı sağlar.</p>
<h3 class='solayasla'>Azot Jeneratörü</h3>
<p>Soğuk hava deposunda mevcut havadan saf azot üreten sistemdir. Havadaki azotu absorbe edip oksijeni dışarı atar. Azot jeneratörü yüksek ölçekli moleküler karbon elek değerine sahip iki adet kaptan oluşur. Moleküler elek belli bir süre boyunca, belirlenen basınca ulaşıncaya kadar havadaki azot moleküllerini emer. Kaplardan biri doyma noktasına eriştiğinde, sistem otomatik olarak vana aracılığı ile diğer kabı devreye alır. Emilen oksijen molekülleri dışarı atılarak doymuş olan kabın basıncı alınır ve yeniden kullanıma hazır hale getirilir. Bu çevirim sayesinde uzun bir kullanım süreci ve yüksek güvenirlik sağlanmış olur.</p>
<h2 class='solayasla'>Atmosfer Kontrollü Depolamanın Avantajları</h2>
<ul class='solayasla'>
	<li>Atmosfer kontrollü depolarda ürünleri çok daha uzun süre saklayabilirsiniz.</li>
	<li>Atmosfer kontrollü depolarda atmosferin kontrolü için herhangi bir kimyasal kullanılmaz ve kesinlikle çevre dostudur.</li>
	<li>Etilen ve oksijen gazının emiliminden dolayı ürünlerin olgunlaşması önlenmiş olur.</li>
	<li>Ürünlerin su kaybı korunduğu için ürün ağırlığından hiçbir şey kaybetmez</li>
	<li>Saklanan ürünlerin kalitesinin artmasından dolayı yurtiçi ve yurt dışı pazarlarda rakiplerinize göre pazarlama konusunda daha fazla avantaj sağlayacaktır.</li>
	<li>Saklanan ürünlerin mevsim dışında da pazarda yer almasını sağlar.</li>
	<li>Ortamdaki oksijenin emilmesinden dolayı haşere varlığının da önüne geçilmiş olur.</li>
