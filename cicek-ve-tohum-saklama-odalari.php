<h3>Hasat Öncesi Faktörler</h3>
<p>Kesme çiçeklerde hasat öncesi kalite kaybını etkileyen koşullar; genetik faktör (çeşit) ve yetiştirme koşullarıdır. Vazo ömrü, kurşuni küfe (botrytis) hassasiyet, çiçeklerin açması, iletim demetlerinin tıkanması ve etilene hassasiyet gibi faktörler çeşitlere bağlı olarak değişir. Sıcaklık, ışık, nispi nem, CO<sub>2</sub>, toprak, sulama, gübreleme, hastalık ve zararlılarla mücadele, hasat zamanı ve budama gibi yetiştirme koşulları vazo ömrü üzerinde önemli etkiye sahiptir. Optimal koşullar altında yetiştirilen bitkilerden elde edilen çiçekler hem daha kaliteli hem de daha uzun vazo ömrüne sahip olur.</p>

<h3>Hasat Sonrası Faktörler</h3>
<p>Kesme çiçeklerde hasat sonrası kalite kaybını ve dayanma süresini etkileyen başlıca faktörler; sıcaklık, etilen, su kaybı ve iletim demetlerinin tıkanması, ön soğutma, suda taşıma, geotropizm, yaprak sararması ve hastalıktır (botrytis). </p>

<h4>Su kaybı ve iletim demetlerinin tıkanması</h4>
<p>Kesme çiçeklerde hasat sonrası ömrü ve kaliteyi etkileyen en önemli faktörlerden biri su dengesidir. Transpirasyon yoluyla kaybedilen suyun saptan çekilen su ile karşılanamaması durumunda çiçeklerde solma meydana gelir. Yetersiz su alma çoğunlukla çiçek saplarının dip kısımlarında fiziksel ve mikrobik tıkanmalardan kaynaklanır. Bunun yanı sıra bazı odunsu sapa sahip kesme gül, waxflower ve kesme yeşillik olarak kullanılan akasya (Acacia holosericea) gibi türlerde iletim demetlerinde kesim sonrası yaralanmaya tepki olarak bazı enzim faaliyetleri sonucunda fizyolojik tıkanma gelişmektedir. Kesme çiçeklerde hem su alımını iyileştirmek hem de vazo suyunda bakteriyel gelişme sonucu sapta oluşan tıkanmayı önlemek amacıyla çiçek solüsyonlarına alüminyum sülfat (200-300 ppm), 8-HOC (8-hydroksikinolin sitrat) (200-600 ppm), 8-HQS (8-hydroksikinolin sülfat) (200-600ppm), gümüş nitrat (10-200 ppm) veya gümüştiyo sülfat (0,2-4 ppm) gibi bakterisitler ilave edilir. İletim demetlerinin tıkanma sorunuyla en sık kesme gül ve gerbera türlerinde karşılaşılmaktadır. Lilium ve kasımpatı iletim demetlerinin tıkanmasına orta derecede hassas olan türler iken karanfil, frezya, lale ve iris’te bu sorunla karşılaşılmaz. Kesme çiçeklere su çektirme işlemi genellikle 4˚C çevre sıcaklığında ılık ve düşük pH’lı (3-5,5) suda 6-12 saat süreyle yapılır.</p>
<p>Çiçeklerin bir çözeltiye yerleştirilmeden önce sap diplerinin 1-2,5 cm’den kesilmesi ve su içinde kalan yaprakların koparılması vazo ömrünü uzatmaktadır. Sap diplerinin kesilme işlemi genellikle havada yapılır. Ancak kuru depolama ve taşıma sonrası saptan iletim demetine hava girişi tıkanmaya yol açtığından dolayı özellikle kuru depolama ve taşıma sonrası kesimlerin su altında yapılması önerilmekte ve suyun mutlaka temiz olması gerekmektedir.</p>

