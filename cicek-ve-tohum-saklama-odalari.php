<p>Etilene hassas kesme çiçek türlerinde etilen zararını önlemek ve/veya azaltmak için; çiçekler optimum gelişme devresinde hasat edilmeli, işleme sırasında çiçeklere fiziksel zararlardan kaçınılmalı, hasattan sonra çiçekler kısa sürede soğutulmalı, işleme ve depo alanlarında havalandırma ve hava sirkülasyonu sağlanmalı, çiçekler etilen üreten meyve ve sebzelerle birlikte depolanmamalı ve anti-etilen ürünler kullanılmalıdır. Anti-etilen olarak kullanılan ürünler: <strong>GTS</strong>, <strong>STS</strong> (gümüş tiyosülfat); saptan çektirme şeklinde uygulanır. İçerdiği gümüş iyonlarının yer altı sularını kirleten ağır bir metal olması nedeniyle günümüzde birçok ülkede kullanımı yasaklanmış olmakla birlikte ülkemizde kullanımı devam etmektedir. Uygulama dozu 1 litre suya 1-3 ml’dir. <strong>1-MCP</strong> (1-metilsiklopropan); EthylBloc ticari ismiyle üretim lisansı alan bu ürün su veya buffer çözeltisi katıldığında ortama bir gaz vermektedir, bu nedenle <strong>1-MCP</strong> uygulanacak çiçekler kapalı bir yerde bulundurulmalıdır. Uygulamada ortam sıcaklığının 24-25°C olması ideal olup uygulama süresi (6-24 saat) doz ve sıcaklığa bağlı olarak değişir. <strong>1-MCP</strong>’nin dezavantajı etki süresinin ortam sıcaklığına bağlı olması ve belirli bir süre sonra çiçeğin yeniden etilene karşı duyarlı hale gelmesidir. Uygulama dozu 1 litre suya 2.5 nl-1 µl’dir. <strong>AOA</strong> (aminooksiasetik asit) ve <strong>AVG</strong> (aminoetooksivinilglisin); her iki anti-etilen ürün çiçeğin içsel etilen sentezini önler, saptan çektirme veya sprey olarak uygulanır. <strong>Potasyum permanganat;</strong> ortamda bulunan etileni absorbe eden ürünleri piyasada filtre, sargı, astar veya kılıf şeklinde satılmaktadır.</p>

<h4>Ön soğutma</h4>
<p>Çiçeklerin üretim veya hasat sonrası ısısının kısa sürede alınması ve depolama sıcaklığına düşürülmesi işlemine ön soğutma denir. Etkili bir soğutma için çiçek kutularında karşılıklı hava delikleri olmalı, ambalajlama ve istif şekli hava akışını engellememelidir. Kesme çiçeklerde uygun bir soğutma yöntemi (pasif soğutma, zorlanmış hava ile soğutma) ile sıcaklık çiçeklerin zarar görmeyeceği en düşük sıcaklığa (0-1°C) düşürülür. Tropik türlerde ise sıcaklık 10°C’nin üzerinde olmalıdır.</p>
