						<p><strong>Çiçek ve tohum saklama odaları</strong>, ürün ya da ürün gruplarının bir arada istenilen muhafaza koşullarını sağlayacak şekilde saklanabilmesi için dizayn edilir. Tohumlarda saklama koşullarında çok fazla çeşitlilik olmasa da canlı bitkiler ve kesme çiçeklerde bu durum aynı değildir. Bu yüzden özellikle süs bitkileri ve kesme çiçeklere daha çok değineceğiz.</p>
<h2>Kesme Çiçeklerde Hasat Sonrası Ömrü Etkileyen Faktörler</h2>
<p>Dünyada üretilen kesme çiçeklerin yaklaşık %25’i üreticiden tüketiciye kadar olan zincirde farklı nedenlerden dolayı kayba uğramaktadır. Çiçek sapı, yaprak ve çiçek organlarındaki kalite kayıpları ya ürünün pazar değerini düşürmekte ya da ürünün satışını engellemektedir. Bu nedenle üreticiden tüketiciye kadar olan zincirde hem kalitenin korunması hem de hasat sonrası kayıpların önlenmesi üretici-tüketici memnuniyeti açısından önemlidir.</p>
<p>Kesme çiçeklerde en önemli kalite kriterlerinden biri vazo ömrü olup vazo ömrünün uzunluğu 2/3 oranında hasat öncesi, 1/3 oranında ise hasat sonrası faktörlere bağlıdır. Farklı kesme çiçek türlerinde 4 günlük taşıma sonrasında çiçeklerin vazo ömrünün ortalama %32 oranında azaldığı belirtilmiştir.</p>

