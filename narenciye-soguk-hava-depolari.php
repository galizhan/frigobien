				<h2>Narenciye Soğuk Hava Depoları</h2>
				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/narenciye-soguk-hava-depolari.webp" alt="Frigobien Narenciye Soğuk Hava Depoları" title="Frigobien Narenciye Soğuk Hava Depoları" />
