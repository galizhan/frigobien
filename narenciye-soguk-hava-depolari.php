<p><strong>Narenciye soğuk hava depoları</strong> CFC içermeyen 40 kg/m³ yoğunlukta soğuk depo panelleri ile yalıtılmaktadır. Çift rejimli soğuk oda grupları ile A++ enerji seviyesinde soğutulan depolarda büyük enerji tasarrufu da sağlamaktadır. Narenciyenin işleme merkezlerine ulaşana kadar geçen süreler içinde uygun koşullarda depolanması gerekmektedir.<br><br>Ülkemizde turunçgillerin %35-40’ı ihraç edilmektedir. Yetiştiricilik, hasat zamanı ve özellikle depolamada yapılan birtakım hatalar ve bilgi eksiklikleri yüzünden ürünlerimiz ve dolayısıyla da ülke ekonomimiz önemli miktarlarda kayıplara uğramaktadır. Ülkemizde turunçgil meyvelerinin hasadının ve muhafazalarının henüz tam ve ayrıntılı olarak bilinmemesi yüzünden, bu ürünlerin %20-30’u üreticiden tüketiciye ulaşıncaya kadar geçen zamanda çürüyüp atılmakta, bu da her yıl milyarlarca liralık zarara neden olmaktadır.<p>
<h2>Narenciye Hasat</h2>
<p>Hasat genelde yaralanmaların en çok olduğu ve patojenlerin en fazla bulaştığı aşamalardandır. Hasat zamanı ve şekli önemli olduğu gibi işçilerin tırnakları, meyveyi hırpalamaları, hatta psikolojik durumları bile hasat esnasında oluşan zararlanmalar üzerine etkilidir. Hasat zamanı saptandıktan sonra, turunçgillerin zedelenmeye meydan verilmeden dikkatle toplanması, sınıflanması ve ambalajının yapılması gerekmektedir. Hasatta azami dikkat ve özen çok önemlidir. Meyvenin o andaki değeri üzerinde önemli bir etki yapmakla kalmaz, bu sırada meydana gelen yaralanmalar ve berelenmeler ile bunların sonucundaki küflenmeler, meyvenin muhafaza süresini de geniş ölçüde etkiler.</p>
<p>Kültürel önlemler, turunçgillerin hasadını ve maliyetini önemli derecede etkileyebilir. Bu bakımdan belki de en önemli faktör ağaçların terbiyesidir. Avrupa ülkelerinde ağaçlar sık bir şekilde dikilerek ve dikkatli budanarak kontrol altında tutulmaktadır. Böylece bütün meyveler normal yükseklikte ve ağırlıkta bir merdiven yardımıyla toplanabilmektedir. Turunçgil hasadı yapılırken yumurtaların kırılmaması için gösterilen hassasiyet, turunçgil hasadında da gösterilmelidir. Hasat sırasında meydana gelen zararlanmalar şu şekilde olmaktadır:</p>
<ul>
<li>Kabukta, toplayıcının tırnağı ile yapmış olduğu çizikler</li>
<li>Meyveyi, toplama kabına bırakırken ya da bu kaplardan daha büyük sandıklara boşaltırken meyvenin yüzünde meydana gelen çatlaklar ve bereler</li>
<li>Meyvenin ağaçtan dikkatsizce koparılması sonucu sapın kopması ya da sapın meyve ile birleştiği yerde meyve dokusunda meydana gelen zararlanmalar</li>
<li>Hasat sırasında toprağa düşen meyvelerde bunların düşerken dallara dokunması ya da toprağa çarpmaları sonucunda meydana gelen zararlanmalar.</li>
</ul>
<p>Turunçgillerde, kabukta meydana gelen berelere ve çiziklere engel olmak için toplayıcının tırnaklarının kısa olması ya da eldiven giymeleri istenir. Turunçgiller ucu küt makaslarla kesilerek toplanmalı, kesim kapsülün (düğme) hemen üstünden kapsülü zedelemeden yapılmalıdır. Kabukta ve kapsülde meydana gelen bu çizikler genelde bu kısımda küflenmeye neden olur ki bunun sonucunda meyvenin tamamı ve büyük olasılıkla yanındaki birkaç meyve de beraber çürür.</p>
<p>Hasadın başlama saati sabahın erken saatleridir ve meyvelerin üzerinden çiy kalkmasına özen gösterilmelidir. Yağışlı havalarda hasat yapılmamasına karşın soğuk havalarda hasada devam edilebilir. Toplamaya etek dallarından başlanmalı, etek dallarındaki ve yüksek dallardaki meyveler ayrı ayrı toplanmalıdır. Toplayıcı meyvelerin 25-30 cm kadar yükseklikten toplama kabına düşmesinde sakınca görmeyebilir. Fakat gerçekte hasat zamanı dayanıklı veya sert olan meyveler dahi bu biçimindeki düşmelerde ciddi bir şekilde zedelenmektedir. Zararlanmalar sadece hasat zamanında olmayıp ambalaj işlemi sırasında geniş ölçüde artabilir. Gerek toplayıcı gerekse yetiştirici, toprağa düşmüş meyveleri de toplama eğilimindedir. Bu gibi meyveler, zedelenmiş olmaları ve küflenmeye eğilimleri nedeniyle genellikle taze olarak tüketime elverişli değildir. Bu tip meyvelerin elden çıkması için bunların diğer meyvelerden ayrılması gerekmektedir.</p>
<p>Turunçgillerde toplama kabı olarak içi süngerle kaplanmış (yastıklı) sepetler kullanılmalıdır. İyi yapılmış hasat ekipmanı, hasat zararlarının en aza indirilmesine geniş ölçüde yardım edebilir. Meyveler sınıflama ve ambalaj evlerine nakledilecekse büyük bahçe sandıklarına boşaltılması istenebilir. Bahçe sandıklarının temiz, düzgün ve sıkı olması iyi sonuç alınması için zorunludur.</p>
<h3>Paketleme Evi İşlemleri</h3>
<p>Bazı ürünler hasattan hemen sonra bahçedeyken pazara gönderilecek şekilde hazırlanır. Genelde ise merkezi bir yere taşınması tercih edilir. Hasat edilen meyveler en kısa sürede uygun koşullarda paketleme evine taşınmalı ve en az 1-2 gün bekletildikten sonra işlenerek ambalajlanmalıdır. Bir-iki günlük bekletme esnasında kabuk biraz su kaybeder bunun sonucu kabuk hücrelerinin zararlanmaları azalmaktadır. Limonlar depolanmadan önce en az 7-10 gün gölgede meyve kabuğundaki suyu atması için bekletildikten sonra ambalajlanarak depoya alınmalıdır.</p>
<p>Paketleme evine gelen ürünler ise aşağıdaki işlemlerden geçirilir.</p>
<ul>
<li>Ön yıkama ve ilaçlama (Drencher)</li>
<li>Sarartma (Satsuma, enter gibi erkenci ürünlerde)</li>
<li>Seçme, Ayıklama</li>
<li>Yıkama</li>
<li>Mumlama ve İlaçlama</li>
<li>Sınıflama</li>
<li>Boylama,</li>
<li>Ambalajlama</li>
<li>Pazara Taşıma</li>
</ul>
<h2>Narenciye Depolama</h2>
<p>Turunçgillerde en ihmal edilen hususlardan birisi de depolamadır. Halbuki, depolamanın çok önemli avantajları bulunmaktadır. Bunlar;</p>
<ul>
<li>Ağaç üzerinde oluşan kaliteyi, meyve tüketilinceye kadar muhafaza etmek,</li>
<li>Pazara düzenli olarak meyve arz edilerek, fiyat istikrarı sağlamak,</li>
<li>Özellikle ihracat için, toplu ve düzenli meyve temin etmek;</li>
</ul>
<p>Bu avantajlardan yararlanabilmek için, meyveler mutlaka, soğuk depolara konularak muhafaza edilmelidir. Turunçgil meyveleri, adi depolarda veya makineyle soğutulan depolarda muhafaza edilirler. Kış aylarında, adi depo olarak havalandırılabilen herhangi bir oda kullanılabilir. Havalar ısındıktan sonra, bu odalarda meyve muhafaza edilmez. Ülkemizde, “yatak limonculuğu” denilen işlem, buna örnektir. Kış aylarında hasat edilen limonlar, Mart ayına kadar üretildikleri yerlerdeki adi depolarda saklanır. Havalar ısınınca, Ürgüp’teki tabii mağaralara götürülür. Bu şekilde limonlar, Ağustos ayına kadar muhafaza edilebilmektedir. Meyveler, adi depoya veya makine ile soğutulan modern depolara konmadan önce, ambalajlanmalıdır. Depolanacak meyveler, ambalajlanmadan önce veya ambalajlama sırasında, hastalıklara karşı mutlaka ilaçlanmalıdır. Aynı şekilde, soğuk depolarda temizlenip ilaçlanmalıdır. Kullanılacak ilaçlar için, resmi kuruluşlardaki uzmanlara başvurulmalıdır.</p>
<p>Soğuk depolara ambalajlanmış meyveler, ambalaj kaplarının arasından hava geçebilecek şekilde istiflenmelidir. Turunçgil meyvelerinin depolanmasında, nispi rutubet genellikle %85-90 olmaktadır. Depo sıcaklıkları ile muhafaza süreleri, türler itibariyle aşağıdaki gibidir.</p>
<ul>
<li><strong>Mandarinler</strong> : 3-4°C’de 2-3 ay</li>
<li><strong>Portakal</strong> : 5-6°C’de 5-6 ay</li>
<li><strong>Altıntoplar</strong> : 7-9°C’de 6-7 ay</li>
<li><strong>Limonlar</strong> : 10-12°C’de 7-8 ay</li>
</ul>
<p>Gerek hasat mevsiminin 9 aylık döneme yayılması, gerekse uzun sayılabilecek bir depolama süresine sahip olmaları nedeniyle, turunçgil meyveleri, taze olarak pazarlanabilmekte ve tüketilmektedir.</p>
