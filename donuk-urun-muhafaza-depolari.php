					<div class="col-md-12">
						<p class='resimyanyazisi'><strong>Donuk ürün muhafaza depoları</strong>, şoklama yöntemi ile kısa sürede çekirdek sıcaklıklarının -18°C'ye düşürülmüş ürünlerin muhafaza edildiği -15°C ile -25°C arası sıcaklıkta olan soğuk hava depolarına denir.<br> Donmuş muhafaza depolarında saklanacak ürünlerin saklama süreleri ürün niteliğine göre ve depolama süresine göre değişmektedir. Depolanan ürünlerin depolama sıcaklığı dikkate alınarak sıcaklığı ayarlanan depolarda saklanan ürünler saklanma süreleri boyunca bozulma riski olmadan uzun süre muhafaza edilmiş olacaktır.<p>					</div>
					<div class="clearfix"></div>
					<div class="col-md-12">
						<p>Gıdalar, şoklama prensiplerine uygun şekilde şoklanırsa, donuk ürün muhafaza depolarında -17,77°C (0°F)’de süre sınırı olmaksızın depolanabilir. Şoklama ve donuk ürün muhafaza şartları sağlanmadığında gıda maddesi bozulabilir, lezzetini ve doğal dokusunu kaybedebilir. Besin maddesinin donma hızı da önemlidir. Çok hızlı bir şekilde dondurulan gıdalar, yavaşça donanlardan daha uzun süre dayanır. Çünkü hızlı dondurma buz kristallerinin oluşmasını önler. Hızlı dondurulmuş meyvelerin yüzeyinde buz kristalleri oluşmaz. Buz kristallerinin oluşması önlenmezse meyvenin hücre duvarı delinebilir. Bu durum, meyvenin doğal tadının ve dokusunun kaybolmasına neden olur. Donma kimyasal reaksiyonları önlemediğinden gıda maddesi kokusu, tadı ve doğal dokusunu kaybedebilir. Özetle; uygun şekilde dondurulmuş olan gıda maddelerini uzun süre sonra tüketmenin sağlık açısından bir zararı yoktur ancak lezzet konusunda aynı garanti verilemez.</p>					</div>
