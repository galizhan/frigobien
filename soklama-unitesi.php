					<h2>Şoklama Ünitesi</h2>
					<p><strong>Şoklama Ünitesi</strong>, gıda güvenliği açısından uzun süre muhafaza edilecek ürünlerin çekirdek sıcaklığını -18°C’ ye kadar şoklayabilecek kapasitelerde üretilir. Şoklanan ürünler daha sonra donmuş muhafaza odasına alınmalıdır. Şoklama işlemi ürün üzerinden yüksek hızda (5 m/s) soğuk hava (-35°C / -40°C) geçirmek suretiyle ürünün dış cidardan başlayarak çekirdeğe doğru donmasını sağlamaktır. Şoklama işlemi mümkün olabilen en hızlı şekilde yapılmalıdır. Ürüne bağlı olarak maksimum sürelere, gıda güvenliği açısından dikkat edilmelidir. Ürün kalınlığı şoklama süresini etkilemektedir. Hava akış hızı şoklama süresini etkilemektedir. Yarı hermetik veya vidalı kompresörlü ve hava soğutmalı kondenserlidir. Cihaz uzaktan kumanda paneline sahiptir. Dış hava şartlarına dayanıklı olarak kasetlenmiştir. İç ve dış ünite azot şarjlı olarak teslim edilir. Elektrik panosu ve tüm otomatik kontrol elemanları ile birlikte montaja hazır olarak sevk edilir. Elekrostatik boyalı çelik galvaniz kabine sahiptir. Kapasiteye uygun yüksek verimli evaporatör dizaynı yapılmaktadır. İhtiyaca göre yer tipi veya tavan tipi evaporatör seçeneği vardır.</p>					<div class="clearfix"></div>
