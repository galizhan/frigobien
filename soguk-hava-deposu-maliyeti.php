						<p class='ikiyanayasla'><strong>Soğuk hava deposu maliyeti</strong>, soğuk hava deposu yatırımı düşünenler için ele alınması gereken en elzem konudur. Neticede soğuk hava deposunun hayata geçirilmesinden kullanılmasına kadar bir çok gider ile karşı karşıya kalacaksınız. Yatırımcıların en başta sorduğu soğrulardan biri olan soğuk hava deposu maliyeti hakkında bilgiler vermek için bu sayfamızı düzenledik. Daha ayrıntılı bilgiler için bizimle iletişime geçebilirsiniz.
<h2>Soğuk Hava Deposu Maliyeti 2019</h2>
<p><strong>Soğuk hava deposu maliyeti</strong> hesaplanırken soğuk hava deposunun ne amaçlar kullanılacağı, hangi iklim şartlarında çalışacağı, soğuk hava deposu kapasitesi ve dolayısı ile ürün sirkülasyonu çok iyi belirlenmelidir. Soğuk hava deposunu oluşturan ekipmanlar da ele alındığında geriye maliyet açıdan enerji giderleri de hesaplandığında soğuk hava deposunu maliyetini çıkarmış oluruz. Örneğimizi <strong>100m2 soğuk hava deposu maliyeti</strong> şeklinde ele alacak olursak, bu soğuk hava deposunun da +4°C de çalışacağını düşünürsek buna göre ekipmanlar seçmeliyiz.</p>

<h2>100m2 Soğuk Hava Deposu Maliyeti</h2>
<p><a href='soguk-hava-deposu.php' title='Soğuk hava deposu'>Soğuk hava deposu</a> için ölçülerimizi 10 metre en, 20 metre boy ve 4 metre yüksek olarak ele alalım. Burada önemli unsurlar, zemin yalıtımı ve duvarlar ile tavanın yalıtımının nasıl yapılması gerektiğidir. Eğer +4°C'de çalışacak bir soğuk hava deposu inşa ediyorsak ve direkt olarak güneş ile teması olmayacaksa duvarlar ve tavan için 80mm poliüretan <a href='sandvic-paneller.php' title='sandviç panel'>sandviç panel</a> kullanmalıyız. Zemin için inşai yalıtım (özel yalıtımlı beton dökülerek yapılır) da kullanılabilir, poliüretan zemin paneli de kullanılabilir. Bu ikisi arasında kesinlikle fiyat farkı olacaktır. İnşai yalıtımda zeminin altına 15cm ila 20cm kadar inileceğinden duvar panellerinin ölçüsü de o ölçüde artmak zorundadır. Yani inşai yalıtım tercih edilirse duvar panellerimiz 4 metre değil 4,20 metre olacaktır. Biz burada zemin için poliüretan zemin paneli kullanacağız. Bu durumda bizim (kısa duvarımız 80m², uzun duvarımız 160m², tavanımız 100m²) toplamda duvarlar ve tavan 340m² 80mm poliüretan sandviç panele ihtiyacımız var. Yine 100m² 80mm poliüretan zemin paneline ihtiyacımız var.</p>

<p>Yukarıdaki hesapları listelersek;</p>
<ul>
<li>340m² 80mm poly/poly kilitli sandviç panel</li>
<li>340m² 80mm poly/poly kilitli sandviç panel sarf malzemeleri (slikon, poliüretan vb.)</li>
<li>340m² 80mm poly/poly kilitli sandviç panel aksesuarı</li>
<li>340m² 80mm poly/poly kilitli sandviç panel montaj işçiliği</li>
<li>100m² 80mm plw/poly zemin paneli</li>
<li>100m² 80mm plw/poly zemin paneli sarf malzemeleri (slikon, poliüretan vb.)</li>
<li>100m² 80mm plw/poly zemin paneli aksesuarı</li>
<li>100m² 80mm plw/poly zemin paneli montaj işçiliği</li>
<li>90X190 Menteşeli soğuk oda kapısı</li>
<li>90X190 Menteşeli soğuk oda kapısı montaj işçiliği</li>
<li>Soğutma Grubu (*Soğutma grupları dış ve iç üniteden oluşur. Evaporatör ve kondenser.)</li>
<li>Soğutma Grubu montajı için sarf malzemeleri (bakır boru vs, hat mesafesine göre fiyat değişir.)</li>
<li>Soğutma Grubu montajı işçiliği</li>
<li>Aydınlatma</li>
<li>Nakliye</li>
</ul>
<p>Yukarıdaki listedekiler basit bir soğuk hava deposunun maliyetini oluşturanlar kalemlerdir. Bu kalemlerin her biri ayrı ayrı hesaplanarak fiyat listesine eklenir. Ancak her soğuk hava deposunun yapımından önce keşif yapılmalıdır. Çünkü keşif sonucunda yapılması zorunlu olan işler tespit edilebilir. Bunların kim tarafından yapılacağı belirlenir. Soğuk hava deposunun yapımına uygun bir yer olup olmadığının tespiti çok önemlidir. Bunun için keşif esnasında tarafımızca bir montaj keşif formu doldurulur. Bu forma şu sorulara cevaplar aranır.</p>
