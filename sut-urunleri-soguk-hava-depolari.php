						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/sut-urunleri-soguk-hava-depolari.webp" alt="Frigobien Süt Ürünleri Soğuk Hava Depoları" title="Frigobien Süt Ürünleri Soğuk Hava Depoları" />
