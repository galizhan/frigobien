						<p><strong>Süt ürünleri soğuk hava depoları</strong>, sadece sütün saklanmasın ibaret olmadığı gibi çiğ sütten, tereyağına kadar tüm ayrıntılara burada yer vermeye çalıştık. Frigobien olarak konu ile ilgili çok daha ayrıntılı bir şekilde bilgilendirmeler ve çözümler için her zaman hazırız. Bizi aramaktan kesinlikle çekinmeyin.</p>
<h2>Çiğ Sütün Soğukta Depolanması</h2>
<p>Sütün bozulmasında rolü olan etkenlerin en önemlisi kuşkusuz mikroorganizmalardır. Değişik kaynaklardan süte bulaşan mikroorganizmalar, çeşitli fermantasyonlara, parçalanmalara neden olmakta ve bu etkinliklerin sonucunda sütün özellikle duyusal niteliklerinde değişiklikler gözlemlenmektedir. Sütte mikroorganizmaların etkinliği, sütün çeşitli süt mamullerine işlenişini de güçleştirmekte, ayrıca kimi zaman tehlikeli hastalıklara ve salgınlara da yol açabilmektedir.</p>
<p>Sütün mikroorganizmalara bulaşması kaçınılmaz olduğuna göre, onun doğal niteliğini koruyabilmenin başlıca yolu, bakteri etkinliğini frenlemektedir. Bunun için de sütü sağar sağmaz hemen 10°C'nin altına soğutmak, bakteri etkinliğinin yoğun olduğu sıcaktan uzaklaştırmak gerekir. Ancak, psikrofil bakterilerin etkinliği 4°C'nin altında bile uzun süre depolamayı engellemektedir. Bu nedenle soğutulan sütlerin en iyisi ilk sağımdan en geç 24 saat sonra işlenerek mamul haline dönüştürülmesi gerekmektedir. Çünkü düşük derecelerde saklanan sütlerin peynir mayası ile pıhtılaşma süreleri uzamakta ve böyle sütlerden peynir yapıldığında, özellikle peynir suyu geç ayrılmakta, pıhtı gevşeyerek işlenmesi güçleşmektedir. Ancak bu sakıncalar peynire işlenen süte CaCl<sub>2</sub> ilavesi ile büyük ölçüde giderilebilir. Öte yandan soğukta saklama sırasında serbest yağ asitleri de artmakta ve özellikle 6, 8, 10 ve 12 karbonlu olanları sütte ve tereyağında acılaştırmaya neden olmaktadır. Sütün soğutma hızı da hidroliz olaylarını etkilemektedir. Örneğin süt30°C'den 5°C'ye 10 saniye içinde soğutulduğunda hidrolitik parçalanmalar en az, buna karşın soğutma süresi 25 dakika olduğunda çok yüksek düzeylere ulaşmaktadır. Soğutma tanklarındaki karıştırıcılar da lipoliz olayını hızlandırıcı etki yapmaktadır. Karıştırma sonucu büyük yağ küreleri parçalanmakta ve lipaz enzimi aktivitesi yükselmektedir. Ayrıca soğutma sırasında sütün çözünebilir ve kolloidal tuzları arasındaki mineral madde dengesi değişmekte ve bu durumda peynire işlenecek sütlerin pıhtılaşma süreleri uzamakta, pıhtı gevşek olmakta ve kayıplar fazlalaşmaktadır.</p>
<p>Değerlendirilinceye değin sütün tanklarda ve güğümlerde soğutularak saklanması ve aynı şekilde üretim yerleri ile süt toplama merkezlerinden fabrikalara ulaştırılması zorunlu bir işlem olmakla beraber, yukarıda da bir ölçüde değinildiği gibi, soğutmanın, belirli bir süreden sonra sütün yapısında olumsuz değişmelere neden olduğu ve bu gibi sütlerden standart kalitede içme sütü ile süt mamulleri üretiminde sorunlar ortaya çıktığı görülmektedir.</p>
<strong>Soğukta saklanması sırasında sütte oluşan değişiklikleri en alt düzeye indirgemek için şunlar önerilebilir:</strong>
<ul>
<li>Sabah ve akşam saatleri arasındaki süre kısaltılmalıdır.</li>
<li>Süt, sağımdan sonra en kısa sürede soğutulmalıdır.</li>
<li>Soğutulan sütün donmamasına dikkat edilmelidir.</li>
<li>Sür, 1-2 günden fazla soğukta depolanmamalıdır.</li>
<li>Sütün karıştırılması çok hızlı yapılmalıdır.</li>
<li>Sütün mamullere işlenmesine kadar geçen süre en aza indirilmelidir.</li>
</ul>
<h2>İçme Sütünün Soğukta Depolanması</h2>
<p>İçme sütü denildiğinde çoğunlukla pastörize ve sterilize, diğer bir deyişle ısısal işlem uygulanmış sütler anlaşılmaktadır.</p>
<p>Isısal işlem görmüş sütün depolama, taşıma ve tüketim aşamalarında, mikroorganizmalar, ksantin oksidaz ve benzeri enzimler, ışık ve oksijen gibi etmenler, sütün içerik maddelerini, koku ve tadını değiştirebilirler. Bu nedenle depolamada sütün gün ışığından, özellikle ultraviyole ışınlarından korunması gerekir. Sütü ışığın olumsuz etkilerinden korumak için, depolar ışık sızmayacak biçimde yapılmalıdır. Gerek güneş, gerekse yapay ışığa karşı sütün korunması, ışık geçirmez ambalajlarla, kahverengi cam şişelerle sağlanabilir. Sterilize sütlerin +4°C - 10°C'de saklanması ömrünü arttırır. Buna rağmen UHT yöntemiyle elde edilen bir sterilize süt, kalitesinden yitirmeksizin 15°C - 20°C'de en az 1,5 ay, klasik yöntemle sterilize edilen süt ise 15°C - 20°C'de en az 3 ay dayanır.</p>
<h2>Yoğurdun Soğukta Depolanması</h2>
<p>
Yoğurt ve ayran gibi süt mamulleri depolama sürecinde küf, maya ve L.bulgaricus gibi mikroorganizmaların çoğalması, enzimatik etkinliklerle acılaşmanın ortaya çıkması, bazı kimyasal değişiklikler sonucu proteinlerin su tutma kapasitelerinin değişmesi ve yağın oksidasyona uğramasıyla zamanla tüketilemez hale gelirler. Bu nedenle söz konusu ekşi süt mamullerinin depolanma süreleri oldukça sınırlıdır. Eğer yoğurt hijyenik koşullar altında imal edilmişse;</p>
<ul>
<li>+7°C / +10°C'de 1 haftanın altında,</li>
<li>+5°C'de 1-2 hafta,</li>
<li>0°C / +1°C'de 3 - 6 hafta</li>
</ul>
<p>kadar depolama ömrüne sahip olabilir.</p>
<p>Yoğurdun daha uzun süre saklanabilmesi amacı ile dondurulması olumlu sonuçlar vermemektedir. Dondurarak saklanan yoğurtların biyolojik niteliklerinde önemli değişiklikler ortaya çıkmamasına karşın, çözünme sonucu duyusal nitelikleri, özellikle yapısı önemli ölçüde bozulmaktadır.</p>
<h2>Koyulaştırılmış Süt ve Mamullerinin Depolanması</h2>
<p>Koyulaştırılmış süt ve mamuller +15°C'de 2-3 yıl kadar depolanabilir. Daha uzun süre ve daha yüksek sıcaklıklarda depolama bu tür mamullerde jelleşme ve topaklaşmaya yol açabilir.</p>
<p>Koyulaştırılmış sütleri dondurarak da uzun süre saklamak olanaklıdır. Geçmiş yıllarda, özellikle savaş dönemlerinde bu tür uygulamalar yapılmıştır. Ancak uzun süre dondurulmuş durumda saklanan sütlerde, normal sütlerde olduğu gibi çeşitli duyusal, fiziksel ve kimyasal bozulmalar ortaya çıkmıştır.</p>
<h2>Kurutulmuş Süt Mamullerinin Depolanması</h2>
<p>Kurutulmuş süt mamullerinde ne kadar az su varsa ve su aktivitesi ne kadar düşükse; mikrobiyolojik ve enzimatik etkinlikler ve kimyasal olaylar son derece yavaş seyreder. Çoğu durumlarda kurutulmuş süt ve mamullerinin düşük derecelerde depolanması zorunluluğu vardır. Şöyle ki; bilindiği gibi yüksek derecelerde elde edilen süt tozları unlu mamuller için; orta derecede ısıl işlem görmüş tozlar rekonstite koyulaştırılmış sür üretiminde ve düşük derece üretilmiş olanlar da rekonstite içme sütü ve rekonstite sütten peynir imalatında kullanılırlar. İşte bunlardan düşük derecede ısıl işlem görerek elde edilen ve yağ içeren süt tozları. Kurutma işlemi sonrası hemen soğutulmalıdır.</p>
<p>Teknolojisine uygun bir şekilde üretilmiş ve hava, su buharı ve ışık geçirmez bir materyale havası alınarak veya invert bir gazla paketlenmiş yağsız bir süttozu, kalitesinden yitirmeksizin, 15°C'de 2 yıl depolanabilir. Bu tür süt mamulü için maksimum depolama süresi 4 yıldır. Buna karşın yağlı süttozlarında bu süre 15°C'de 1-3 yıldır.</p>
<h2>Dondurmanın Depolanması</h2>
<p>Dondurma, bileşimce zenginleştirilmiş sütün şeker, harç ve aroma maddeleri, bazen de yumurta katılıp dondurulması sureti ile elde edilen bir süt mamulüdür.</p>
<p><strong>Dondurmalar kıvamlarına göre iki ana grupta toplanabilir:</strong></p>
<ul>
<li>Yumuşak dondurmalar</li>
<li>Sertleştirilmiş dondurmalar</li>
</ul>
<p>Yumuşak dondurma, dondurma makinesinden çıktığı gibi -5°C, -7°C'de tüketime sunulan; sertleştirilmiş dondurma ise derin dondurma makinelerinde, örneğin soğuk tünellerde -20°C, -30°C'ye değin sertleştirilen dondurmalardır. Yumuşak dondurmalarda, içerdiği suyun yalnızca %30-%50'si dondurulmuş olmasına karşın, sertleştirilmiş dondurmalarda bu oran %80-%90'ı bulur. Böylelikle dondurmanın taşınması ve depolanması kolaylaşır. Burada önemli olan dondurmada küçük kristallerin oluşmasının sağlanması ve depolama sürecinde daha büyük kristallerin oluşumunun önlenmesidir. Böylelikle dilde düzgün pürüzsüz ve cilalı bir yapı etkisi sağlanabilmektedir. Bilindiği gibi, küçük ve stabil bir kristalizasyona büyük donma hızıyla ulaşılabilmektedir.</p>
<p>Bundan başka, dondurma işleminde çok düşük sıcaklıktan yararlanmak, yoğun karıştırma işlemi, dondurma miksinin toplam kuru madde oranının yüksek olması ve bazı yardımcı maddelerin eklenmesi, amaca ulaşmayı kolaylaştırıcı etmenlerdendir. İnce, küçük kristallerin elde edilmesinde, dondurma işleminden önce henüz sıvı haldeki dondurma karışımına kristal aşılanması da yararlıdır. Bunun için dondurma aygıtından gelen uzun bir bölümünün, ön soğutması tamamlanmış mikse geri döndürülmesi yeterlidir.</p>
<p class='yesillinkler'>Sertleştirme işlemi tamamlanan dondurmalar, ya hemen tüketime sunulabilirler ya da -20°C, -25°'de aylarca depolanabilirler. Daha ayrıntılı bilgi almak için faaliyet alanları bölümümüzdeki <a target='_blank' href='dondurma-soguk-hava-depolari.php' alt='Dondurma Soğuk Hava Depoları' title='Dondurma Soğuk Hava Depoları'>Dondurma Soğuk Hava Depoları</a> sayfasını incelebilirsiniz.</p>
<h2>Peynirin Depolanması</h2>
<p>Peynirlerin soğukta depolanmasını ili bölüm halinde ele almak gerekir:</p>
<ul>
<li>Olgunlaşma aşamasında soğukta depolama,</li>
<li>Olgunlaşmış peynirin muhafazası amacıyla soğukta depolama.</li>
</ul>
<p>Bilindiği gibi; ham peynirin oldukça serin koşullarda, bir süre bekletilip, çeşidine özgü tat, koku, görünüm ve kıvam kazanma devresine olgunlaşma denilmektedir. Olgunlaşma çok kere, kalite ve küme halinde, raflarda yapılır. Bazen tenekelere konulmuş salamura içinde de ham peynir olgunlaşmasını tamamlar. Peynir olgunlaşmasında en önemli faktör, uygun sıcaklığın ve bağıl nemin saptanması ve kontrol edilmesidir. Sıcaklık, gerçekte, olgunlaşmayı sağlayan mikroorganizmaların etkinliklerini ayarladığından, olgunlaşma ve dolayısı ile peynir kalitesi üzerinde etkilidir. Laktik asit bakterilerinin gelişmesine uygun olan sıcaklık, propiyonik asit bakterilerinin gelişmesine elverişli olmamaktadır. Bu nedenle kademeli olarak sıcaklık değişikliği yapılmaktadır. Peynir olgunlaşmasında önemi büyük olan bu durum, olgunlaşan peynirin saklanmasında da önemli rol oynamaktadır. Sert peynirlerde sıcaklık azaldıkça olgunlaşma genellikle yavaşlar ve peynirin kalitesi yükselir. Ancak sıcaklığın, peynirin yapısını bozacak seviyeye düşmemesi gerekir. Sert peynirin yapısının bozulmasında -10°C kritik noktadır. Yumuşak peynirler, sert peynirlere göre, daha yüksek derecelerde olgunlaşırlar. Olgunlaşma sıcaklıkları, peynir çeşidine göre değişmekle beraber, +8°C, +10°C arasındadır. Gereken bağıl nem ise, taze sert ve yarı sert peynirler için %75 - %85, eski sert ve yarı sert peynirler için %80 - %90 kadardır.</p>
<p>Olgunlaşma süreleri ise: Sert peynirlerde 3 - 10 ay; Yarı sert peynirlerde 5 - 10 hafta; Yumuşak peynirlerde de 3 - 6 haftadır.</p>
<p>Muhafaza amaçlı soğukta depolamada ise yukarıda anlatıldığı gibi, sıcaklık ve nem önemli rol oynar. Peynirlerin, kalitelerinde yitirmeksizin saklanmalarında peynir grupları arasında büyük dayanıklılık farklılığı vardır.</p>
