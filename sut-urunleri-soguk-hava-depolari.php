<h2>Kremanın Soğukta Depolanması</h2>
<p>Süt kreması bir kaç saat içerisinde dondurmaya veya tereyağına işlenmeyecek ve bir süre bekletilecekse, hemen soğutulup depolanmalıdır. Böyle durumlarda soğutma derecesinin +40°C olması önerilmektedir. Ancak uzun süre depolanacaksa, yüksek derecelerde pastörize edilip en az -20°C'de saklanmalıdır.</p>
<h3>Kremanın Dondurulmasında Dikkat Edilecek Hususlar</h3>
<ul>
<li>Dondurulacak kremanın yağ oranı %40-50 olmalıdır. Daha fazlası yağ sızmasına yol açabilir.</li>
<li>Yağ oranı belirtilen düzeye ayarlanan krema pastörize edilmelidir. Isısal işlemin 95°C, 102°C'de 15 sn süre ile yapılması yeterlidir.</li>
<li>Pastörize krema önce yaklaşık 5°C'ye soğutulmalı, sonra da süratle dondurulmalıdır.</li>
<li>Dondurulacak krema yaklaşık 15-30kg'lık bloklar halinde polietilen torbalar içerisinde, olanaklı ise parşömen kağıdı ile astarlanmış polietilen torbalarda ve dikey plakalı dondurma makinelerinde -35°C'de dondurulmalıdır. Polietilen torbalar, kremayı süblimasyondan ve yüzeysel oksidasyondan koruyabilen bir paketleme materyalidir.</li>
<li>Dondurma işlemi sırasında kremaya hava girmesinden sakınılmalı ve özellikle de Fe, Cu gibi yağ bozulmasını katalize eden metallerin bulaşmasından kaçınılmalıdır.</li>
<li>Eğer krema paslanmaz çelik kaplarda saklanacaksa, bu tük kapların kapaklarının sıkıca kapatılmasına ve donan kremanın hacim değişikliğinden dolayı kabı patlamaması için ağzına kadar doldurulmamasına özen gösterilmelidir.</li>
<li>Depolama sıcaklığı en az -20°C olmalıdır. Ancak en uygun depolama sıcaklığı -30°C, -35°C'dir. Belirtilen derecelerde depolanan kremalar, 12-24 ay kadar kalitelerini koruyabilirler.</li>
</ul>
