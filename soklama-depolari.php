						<p><strong>Şoklama depoları</strong>, <strong>Frigobien</strong>'in özel olarak ciddiyet gösterdiği bir faaliyet alanıdır.</p>
<p>Tüm gıdalar hacimlerinde su barındırır. Eğer gıdayı yavaş yavaş soğutursanız, iç sıcaklık ile dış sıcaklık arasında sıcaklık farkından dolayı gıda içindeki suyun buzlanmadan kristalleşmeye yani karlanmaya geçişi başlar. Buzlanmayla karlanma arasındaki hacim farkı suyun barındırıldığı dokuları tahrip etmeye başlar ve bu molekül seviyesine kadar ulaşır. Bu yüzden çözülmüş gıdalar tekrar dondurulamaz, değişime uğrayan moleküller zehirlenmeye neden olabilirler. Bu durumun gerçekleşmiş olduğunu ürün üzerindeki hem şekil farkından hem de tat farkından hemen anlaşılabilir.</p>
<p>Gıdaları, doğal özelliklerini kaybetmeden, yani moleküler yapısı bozulmadan dondurabilmek ve uzun süreli saklayabilmek için -45°C'de şoklayabilmek gerekir. Hatta son yıllarda gelişmiş ülkelerde yapılan araştırmalara göre gıdaların tüm hücre içi biyokimyasal süreçlerinin ve proteinlerin denatürasyonunu durdurabilmek için -60°C de şoklama yapılmasının, organoleptik kalitenin maksimum düzeye çıkarabileceğini göstermiştir. Böylece -60°C'lik bir sıcaklıkta şoklama işlemi, balık, havyar, deniz ürünleri, sığır eti, kümes hayvanları, hazır gıda ürünleri, meyveler, sebzeler ve diğer gıda ürünlerinde gıda kalitesini mükemmel bir şekilde muhafaza eder.</p>

<h2>Frigobien'in Ürettiği Şoklama Depolarında Gıda Şoklamanın Avantajlar</h2>
<ul>
<li><strong>Gıdaların şoklanması</strong>, balık, havyar, deniz ürünleri, sığır eti, kümes hayvanları, hazır gıda ürünleri, meyveler, sebzeler ve diğer gıda ürünlerinde gıda kalitesini mükemmel bir şekilde muhafaza eder. Böyle bir yöntem, geleneksel koruma yöntemleriyle karşılaştırıldığında, gıdaların besin değerlerinin en yüksek seviyede korurken, gıda ürünlerinin uzun süreli ve zahmetsiz saklanmasını sağlar.</li>
<p>Hiçbir kimyasal madde kullanılmadığı için, teknolojimiz, gıdaların birinci sınıf kalitede ekolojik olarak en iyi şekilde depolanmasını sağlar.</li>
<li>-45°C / -60'de saklanan derin dondurulmuş ürünlerin raf ömrü, konvansiyonel sıcaklıklardan çok daha uzundur.</li>
<li>Odadaki nem sürekli olarak% 0 - 2 aralığında tutulur, bu da buz çözme işlemini gereksiz kılar.</li>
<li>Mühendislik çözümlerimiz, soğutulmuş bölme içindeki sıcaklığın eşit dağılımını sağlar. Böylece, ürünler, oda içindeki konumlarına bakılmaksızın eşit olarak soğutulur.</li>
<li>Çalışma havası olarak doğal havayı kullanmak, bakım maliyetlerini düşürür çünkü soğutucu akışkanların yeniden doldurulması gerekmez, çevre üzerindeki insan etkisini en aza indirir ve geleneksel sistemlere en güvenli alternatif olarak hizmet eder.</li>
</ul>

<h2>Gıdaların Şoklanması Aşamaları</h2>
<ul>
<li><strong>Ön Soğutma:</strong> Şoklama makinesinin gücünü hafifletmek ve dondurma hızını artırmak için ürünler ön soğutmadan geçirilir. Bu işlemde gıda, donmadan donma sıcaklığına yaklaştırılır.</li>

<li><strong>Şoklama İşlemi:</strong> Şoklanmanın hedefi gıdanın çekirdek ısısı dediğimiz iç ısısını dış ısıyla beraber aynı anda soğutulmasına yöneliktir. Buradan anlaşılacağı gibi gıdanın ilk ısısı çok önemlidir. Şok odaları tasarlarken gıdanın cinsi, şekli, paketlenmesi, kasalardaki konumu, ısıl yükü gibi değerlerin yanı sıra ürün etrafındaki hava sirkülasyonunun çok iyi yönetilmesi gerekir. Aksi halde buna şoklamak değil dondurmak denir ve evdeki buzdolabınızdan hiç bir farkı olmaz.</li>
<li><strong>Donuk Muhafaza (Donmuş Muhafaza):</strong> Çekirdek ısısı -18°C'ye gelen ürünler şoklama işleminden sonra donuk oda dediğimiz muhafaza odalarına alınır ve burada -18°C'de saklanır.</li>
