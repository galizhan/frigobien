<h2>Soğutma ve Dondurma </h2>
<p>Et ve ürünlerine uygulanan en yaygın ve etkin muhafaza yöntemlerindendir. Etin donma noktası –1,5°C ile –1,7°C arasındadır. Dondurulmanın derecesine ve şekline bağlı olarak etlerin muhafaza süreleri az ve çok arttırılabilir. Etin donma noktasının üstündeki ısı derecelerinde saklanmasına soğukta muhafaza, altındaki sıcaklık derecelerinde saklanmasına da dondurarak muhafaza denir. Mekaniksel soğutma sistemi, fiziksel bir kanun uyarınca yaratılır. Amonyak ve benzeri düşük kaynama noktasına sahip diğer soğutucu ajanları sürekli olarak sıvı halden gaz, gaz halinden de sıvı hale dönüştürülebilen donanımlara soğutucu sistem adı verilir. Amonyağın yanı sıra soğutma sanayiinde kullanılan soğutucuların başlıcaları freon-12, kuru buz (C<sub>2O</sub>) ve sıvılaştırılmış azottur. Bunlar sırasıyla -20°C, -79°C ve -196°C kaynama noktasına sahiptirler.</p>
