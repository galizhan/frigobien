<p>Kırmızı et soğuk hava depoları, kırmızı etin işlendikten sonra muhafaza edildiği -18°C sıcaklıktaki depolardır. Kırmızı et entegre tesislerinde +12°C'de işlenen etin, en az 6 saat 0°C'de ön soğutmaya tabi tutulması, yine aynı derecede 24 saat soğutma işlemine tabi tutularak -40°C'de 6 saat şoklanması ile devam eder. Bunu takiben kırmızı et soğuk hava depolarında -18°C'de muhafaza edilir. -18°C sıcaklıkta tutulması gereken bir kırmızı et soğuk hava deponun en az 150mm kalınlıkta soğuk oda paneli ile yalıtılması gerekmektedir.</p>
<em>Etin bozulma nedenlerini oksijen, su, ışık, ortam sıcaklığı, ette faaliyet gösteren enzimler ve mikroorganizmalar olarak gösterebiliriz.</em>
<div class=''><br></div>
<h2>Et işlenmesi ve muhafazasının amaçları</h2>
<ul>
<li>Ette bozulmaya yol açan mikroorganizmaların engellenmesi</li>
<li>Enzimlerin inaktif hale getirilmesi</li>
<li>Dış etmenlerden korunması</li>
<li>Raf ömrünün artırılması</li>
<li>Et ürünlerinin üretimi</li>
</ul>

<h2>Soğutmada önemli faktörler: </h2>
Sıcaklık: Her 10°C düşüş dayanma süresini 2-3 kat artırır.
Bağıl nem: Su kaybını önler, bakterilerin çoğalmasına neden olabilir.
Et ve ürünlerinde soğutma +10°C ile -1°C arasında yapılır. Et -1,5°C altında donmaya başlar

