<h2>CO2 atmosferinde soğuk depolama</h2>
<ul>
<li>Dayanma süresini iki katına çıkarır</li>
<li>Bakteriyostatik etki sağlar</li>
<li>Renk stabilitesini artırır</li>
<li>Yağ oksidasyonunu geciktirir</li>
</ul>

<h2>Soğutma Sistemleri</h2>
<ul>
<li>Yavaş ve basamaklı soğutma</li>
<li>Hızlı soğutma</li>
<li>Şok soğutma</li>
</ul>

<h2>Yavaş ve basamaklı soğutma</h2>
<ul>
<li>Küçük işletmelerde uygulanır</li>
<li>Karkas ön soğumaya bırakılır, sonra soğuk depoya alınır</li>
<li>Etin olgunlaşması hızlıdır fakat kuruma meydana gelir</li>
</ul>

<h2>Hızlı soğutma</h2>
<ul>
<li>Etin sıcaklığı +4 C’ye düşürülür</li>
<li>Karkas büyüklüğüne göre 12-24 saat arasında gerçekleşir</li>
<li>0°C’de %85 bağıl nemde hava akım hızı 1-4m/sn olarak ayarlanmalıdır</li>
<li>Hızlı soğutma sonunda et hemen kullanılmayacaksa soğuk depolamaya geçilmelidir</li>
<li>Hızlı soğutulan etlerde PSE et oluşumu rizikosu vardır</li>
</ul>

<h2>Şok soğutma</h2>
<ul>
<li>Ortam sıcaklığı –8°C, bağıl nem %90, hava akım hızı 1-4 m/sn olarak ayarlanır</li>
<li>2 saat içinde ortam sıcaklığı 0°C’ye getirilir ve yüzey donması önlenir</li>
<li>Büyük işletmelerde soğuk tünellerde yapılır</li>
<li>Dana ve koyun gibi küçük karkaslı hayvanlar için uygun değildir</li>
<li>Soğuk kısalması ortaya çıkar</li>
</ul>

