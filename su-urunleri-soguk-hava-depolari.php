					<div class="col-md-12">
						<strong>Su ürünleri soğuk hava depoları</strong>, <strong>Frigobien</strong>'in her türlü tarımsal ve hayvansal gıdada olduğu gibi profesyonel olarak çözüm ürettiği faaliyet alanlarından biridir.
<p>Beslenme bakımından su ürünleri, içerdiği azotlu bileşiklerden dolayı, yüksek besin değerine sahip gıdalar arasında yer almaktadır. Ancak su ürünleri bozulmaya karşı hassas olduklarından, avlandıktan hemen sonra taze olarak tüketilmeli ya da orijinal tazeliklerini mümkün olduğunca korumalarını sağlayacak önlemlerin alınması gerekmektedir. Bu amaçla kullanılan yöntemlerin başında soğutma ve dondurma teknolojisi gelmektedir. Soğutma ile su ürünleri -1ºC/+4ºC’de soğukta muhafaza edilebilmekte; ancak soğuk depolama sırasında mikroorganizmaların gelişimi ve biyokimyasal olaylar yavaşlatıldığından su ürünlerinin muhafaza ömrü sınırlı olmaktadır. Dondurma ile mikroorganizmaların gelişimi tamamen önlenmekte, biyokimyasal olaylar ve enzim aktivitesi yavaş bir hızla devam etmektedir. Buna bağlı olarak tazeliğini uzun süre koruyabilen ve daha kaliteli olan su ürünlerinin elde edilmesi mümkün olmaktadır.</p>
<p>Yeni tutulmuş balıkların muhafaza sürelerini uzatmak için soğutulması düşüncesi ilk olarak 18. yüzyılda som balıklarının deniz yolu ile İskoçya’dan Londra’ya taşınması sırasında ortaya çıkmıştır. Fakat bu metodun daha öncesinde Çinli balıkçıların doğal olarak buz tutan göllerden, akarsulardan ve havuzlardan toplanan buzları kullandıkları belirtilmektedir. Buz ilk olarak 1850 yıllarında İngiliz balıkçı teknelerinde kullanılmış ve 19. yüzyılın sonlarına doğru mekanik olarak üretilen buz, balıkçı teknelerinde denenmiştir. Bundan sonra birtakım sistemler geliştirilmiş olup, bunların hiçbirisi balıkların soğutulmasında kullanılan buz kadar faydalı olmamıştır. Su ürünlerinden gerekli ölçülerde yararlanabilme imkanları bunların tüketilmesine kadar kalitenin korunması ile gerçekleşmektedir. Balık avcılığının fazla olduğu dönemlerde balıkların kalitesinin bozulmadan tüketiciye ulaştırılması uygun şartlarda soğuk depolama ile mümkün olmaktadır</p>
<p>Soğutma, balık sıcaklığının donma noktasının hemen üzerindeki sıcaklık olan 0ºC’ye düşürülme işlemidir. Soğutmadaki amaç koruyucu katkı maddesi kullanmadan gıdayı doğal haline en yakın şekilde korumaktır. Sıcaklığın düşürülerek ortamdaki mikroorganizmaların faaliyetlerinin azaltılması veya durdurulması, normal koşullarda oluşan fiziksel, kimyasal ve biyokimyasal olayların mümkün olduğunca önlenmesi soğutmanın ana amacıdır. Soğutma bozulmayı tamamen durdurmaz, fakat belirli bir süre geciktirilmesini sağlamış olur.</p>
<p>Su ürünlerinin depolama süresini uzatmak amacıyla yapılan dondurma işlemi, bozulma ve ekonomik kaybın en aza indirilmesinde, ayrıca halk sağlığı problemleri ve gıda kaynaklı patojenlerin kontrol edilmesinde çok önemli rol oynamaktadır. Çok düşük sıcaklık derecelerinde (-30°C) enzim aktivitesi durur. Genelde dondurma ve dondurarak depolama sırasındaki sıcaklık -18°C ve bunun altındaki sıcaklıklardır. Mikroorganizmalar -5/-8°C’ye kadar, mayalar -10/-12°C’ye kadar, küfler ise -12/-18°C’ye kadar biyolojik aktivitelerini sürdürebilirler. Mikrobiyal bozulma olmaksızın -18°C ve bunun altındaki sıcaklık derecelerinde ürünler depolanabilirler. Gıdalarda su çok sayıda çözünmüş madde içeren çözelti niteliğindedir. Gıdanın donma noktası gıdanın içerdiği sudaki çözülmüş maddenin cinsi ve miktarına bağlı olarak 0°C’nin altındaki bir sıcaklık derecesidir. Etin içerisindeki suyun en küçük donmuş zerrelerin sayısı mümkün olduğu kadar yüksek olmalıdır. Buna dondurma tekniğinde maksimal kristalizasyon denir. Dondurma yerinde dıştan ortaya doğru bütün balıkta dondurulmuş su kristallerinin sayısının maksimal düzeyde olması için ısının 30 dakikada 0°C’den -4°C’ye kadar düşürülmesi gerekmektedir.</p>

<h2>Dondurarak Muhafazayı Etkileyen Faktörler</h2>
<p>Dondurarak muhafazayı etkileyen faktörlerin başında; materyal (ham madde), ön işlemler, dondurma sıcaklığı, glazeleme, depo sıcaklığı, depo şartları ve süresi gelmektedir. Su ürünlerinin kaliteli ve güvenilir bir şekilde dondurulması için bu noktalardaki kritik kontrol noktaları göz önünde bulundurulmalı ve gerekli tedbirler alınmalıdır.</p>

