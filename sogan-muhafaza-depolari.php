				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/sogan-muhafaza-depolari.webp" alt="Frigobien Soğan Muhafaza Depoları" title="Frigobien Soğan Muhafaza Depoları" />
