					<div class="col-md-12">
						<p style="text-align: justify;" class="resimyanyazisi"><p class='resimyanyazisi'><strong>Soğan muhafaza depoları</strong>nda başarılı bir şekilde kuru soğan depolamak için çeşit seçimi, kültürel işlemler, depo koşulları gibi üç önemli etkenin göz önünde bulundurulması gerekir.<br/>
Uzun süre depolanacak kuru soğanlarda çeşit seçimi, önemli bir etkendir. Üreticinin istediği iç ve dış pazar isteklerine uygun, depolama süresi belirlenen çeşitler seçilmelidir. Ülkemizde Banko soğan, Valanciana soğan, Keş soğan, Fito delfos soğan, Swale F1 soğan, Tan 88 soğan, Bereket soğan, Songur soğan başta olmak üzere özel adları ile bilinen bir çok kuru soğan çeşidi vardır. Genel olarak kuru soğanların bütün, sağlam, temiz, don vurmamış, kuru saplı olması ve en az iki kat kuru dış kabuğu bulunması istenir.</p></p>
					</div>
					<div class="clearfix"></div>
					<div class="col-md-12">
						
<h2>Soğan Hasat</h2>
<p>Kuru soğanlarda tüketimlerini engelleyecek çürüklük ve bozukluklar, gözle görülebilir yabancı madde, ıslaklık, yabancı tat ve koku bulunmamalıdır. Kuru soğan sağları düzgün şekilde kesilmeli veya burulmalı ve 4 cm’den daha uzun olmamalıdır. Kabuk yapısı ve dış kabuk sayısı depolamada önemli bir özellik olup, kalın ve çok sayıda dış kabuğa sahip sıkı etli, kalın boyunlu soğan çeşitleri saha fazla dayanıklıdır. Kabuk yapısı depo içerisinde mantarların bulaşması açısından da önemlidir. Kışlık çeşitlerde kabuk sayısı 2 – 5 arasındadır. Genel olarak kuru maddesi %10’dan yüksek olan, protein ve karbonhidratlarca zengin acılığı fazla çeşitler uzun süre depolamaya uygundur.</p>
<p>Soğan depolamada etkili olan kültürel işlemler arasında ekim, dikim zamanı, sulama, gübreleme, hastalık ve zararlılar ile mücadele, hasat ve kurutma sayılabilir. Soğanlarda olgunluk, yaprakların sararması ile başlar ve boyun kısmının yumuşayarak yana yatması ile son bulur. Hasat zamanında toprak üstündeki yeşil bölümün 3’te 2’si (2/3) kurumuş olmalıdır. Soğanların %80’i olgunlaştığında hasat başlar. Erken hasat verim düşüklüğüne ve bunun sonucunda da kurutma ve depolamada zararlara neden olur. Geç hasatta ise yumruda güneş yanıklığı, çiğ-kırağı nedeni ile kabuk dökülmeleri oluşur.</p>
<h2>Soğan Depolama</h2>
<p>Depolanacak kuru soğanların olgunlaşmaları ve istenilen kabuk renginin oluşması için kurutma yapılması gerekir. Kurutma işlemi tarlada veya depoda yapılabilir. Tarlada kurutma yöntemi daha çok kullanılır. Kurutmada yumrular, doğrudan güneş ışığına maruz kalmayacak şekilde dizilmeli ve yağışlı günlerde yumruların üzeri örtülmelidir. Kurutma işlemi ortalama olarak 15 – 20 gün sürer. Kurutma işlemi depoda da yapılabilir. Hasattan sonra depoya getirilen soğanlar eğer ıslak ise, her bir tondan ortalama olarak 45-50 kg suyun soğandan alınarak kurutulması için, depoda üç hafta boyunca iyi bir havalandırma yapılması gerekir.</p>
<h2>Sonuç</h2>
<p>Kurutma için kullanılan havanın %60 bağıl nem ve 20-28°C sıcaklıkta olması önerilir. Üç hafta süren kurutma işlemi sonucunda, deponun sıcaklığı düşürülerek bağıl nem seviyesi yükseltilir. Kurutma işlemi 3 gün içinde de yapılabilir. Bu durumda deponun sıcaklığı 30°C, hava hızı ise ton başına 400-500 m³/h olmalıdır. Daha önce belirtildiği gibi kurutmada amaçlanan soğan yumrusunda bulunan %87,5 oranındaki suyun %5’lik kısmının alınmasıdır.</p>
