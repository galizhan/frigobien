					<div class="col-md-12">
						<p class="ikiyanayasla resimyanyazisi"><strong>Soğuk ürün muhafaza depoları</strong>, gıda depoculuğunda soğuk zincirin kırılmasının önüne geçen önemli faktörlerden biridir. Depolanacak ürünlerin üretiminden hemen sonra bekletilmeden soğutma işlemine tabi tutulması gerekir.<br>
Ürünün sıcaklığı mümkün olan en kısa sürede depo sıcaklığına düşürülmelidir. Ancak bu suretle gıda maddesinin hücre şeklini değiştirmesi minimum seviyeye indirilebilir. Ayrıca soğutma sırasında ürünün nem kaybının asgari seviyede tutulması amacıyla ortamın bağıl nem miktarının uygun şekilde ayarlanması gerekmektedir. Gıda maddeleri, tazeliğinin korunmaları ve raf ömrünün artırılmaları amacıyla soğuk ürün muhafaza depolarında saklanır. Aşağıda bazı gıda maddelerinin soğuk depolama koşullarına dair örnekler verilmiştir.</p>
					</div>
					<div class="clearfix"></div>
					<div class="col-md-12">
						<p>+4°C / 0°C sıcaklığın sağlandığı depoların izolasyonu, 80mm kalınlığında poliüretan sandviç panel ile sağlanır ve soğuk muhafaza depoları olarak adlandırılırlar. En yaygın tercih edilen depolardır. Soğuk muhafazaya ihtiyaç duyabilecek tüm işlenmiş gıdaların muhafazası için tesis edilir. İşlenmiş gıdalarda bozulmaya neden olabilecek bakteri üremesine engel olmak, enzim aktiviteleri ve oksidasyonu düşürmek temel sebep olarak gösterilebilir. Taze süt ve süt ürünleri, et ve işlenmiş et ürünleri ve taze deniz mahsülleri için tercih edilir.</p>					</div>
					
