					<h2>Transcritical CO&#x2082; Booster</h2>
					<p><strong>CO&#x2082; Transkrit Booster</strong>,süpermarketler ve her türlü gıda perakende mağazası için soğutma sistemleri, son birkaç yıldır çevre dostu doğal soğutucu akışkanların kullanımına doğru gelişmektedir. Son yıllarda, bu ilerleme, soğutucu olarak karbondioksit (CO&#x2082;) kullanan çok sayıda sistemin kurulmasına yol açmıştır. Bu sistemlerin ilkinde kaskad tipi sistemin düşük sıcaklık bölümünde ikincil bir soğutucu veya doğrudan genleşme (DX) gazı olarak CO&#x2082; kullanıldı. Bu yaklaşımların her ikisi de, eski sistemlerin birincil tarafında veya ikincisinin orta sıcaklık bölümünde bazı sentetik, çevreye zararlı hidroflorokarbon (HFC) soğutucu akışkanlarının kullanımına güvenmeye devam etmiştir. Şimdi tamamen CO&#x2082;'den çıkan bir tür DX sistemi, ülkemizde taleplerin arttığı bir sistem olmaya başladı.</p>

<p>CO&#x2082; transkritik hidrofor sistemleri, geleneksel DX sistemleriyle aynı buhar sıkıştırmalı soğutma döngüsüne dayanır. Aslında, geleneksel sistemlerle aynı ana bileşenleri kullanırlar: kompresörler, buharlaştırıcılar, kondansatörler ve genleşme vanaları. Sonuç olarak, bir hidrofor sisteminin çalışması, geleneksel bir sistemin nasıl çalıştığını bilen herkese tanıdık gelecektir. İki tür sistemin çalışma şeklindeki fark, kullandıkları soğutucu akışkanların doğası ile ilgilidir. R-410A gibi HFC soğutucuları kullanan geleneksel DX sistemleri tipik olarak inç kare başına yaklaşık 400 pound (psig) basınçlarda çalışır. CO&#x2082;'nin (R-744) malzeme özellikleri nedeniyle, onu kullanan sistemler, ortam koşullarına bağlı olarak 1450 psig kadar daha yüksek basınçlarda çalışacak şekilde tasarlanmalıdır. Bu, örneğin, sistemin yüksek tarafında, özellikle daha yüksek CO&#x2082; çalışma basınçları için tasarlanmış bir tür bakır borunun kullanılabileceği anlamına gelir. Sistem için R-744'ün yüksek çalışma basınçları için özel olarak tasarlanmış diğer bileşenlere de ihtiyaç vardır. Bu bileşenler aşağıda daha ayrıntılı olarak açıklanmaktadır.</p>

<p>Transkritik CO&#x2082; Booster sistemlerin enerji tüketimi, kullanıldığı ortam koşullarına bağlıdır. Ortam sıcaklığı düştükçe, CO&#x2082; Booster sistem subkritik aralıkta çalışır ve potansiyel olarak diğer sistem türlerinden daha iyi enerji performansı sunmasını sağlar. Bu, sistemin HFC'leri tamamen ortadan kaldırmasıyla birlikte, sürdürülebilirlik ve düşük işletme maliyetleri elde etmek isteyen müşteriler için etkili bir seçenek haline getirir. Bu fayda kombinasyonu, bir CO&#x2082; Booster sistemin kullanımı için en büyük mantığı sağlar.</p>

<p>Ayrıca, CO&#x2082; Booster, tek bir kompakt ünitede hem orta sıcaklık hem de düşük sıcaklık kapasitesine sahiptir. Ünitedeki kompresörler çok çeşitli yükleri alacak şekilde boyutlandırılabilir. Bir mağazanın orta ve düşük sıcaklık yükleri için ayrı sistemler yerine her ikisi için yalnızca bir sistem gereklidir. Geleneksel bir sistemin sadece bir kısmı için ihtiyaç duyulacak yaklaşık aynı miktarda alanda, bir hidrofor sistemi mağazanın tüm soğutma ihtiyaçlarını karşılayabilir. Bir mağazanın tamamını tek bir sistemde çalıştırmak için gereken ekipmanı azaltarak, süpermarket gibi işletmeleri yerden tasarruf edebilir ve operasyonlarını basitleştirebilir hale getirecektir.</p>


<h2>Sistem Nasıl Çalışır</h2>
<p>CO&#x2082; Booster sistemlerin bazı yönlerine, geleneksel DX sistemlerinin nasıl çalıştığını bilen herkes aşina olacaktır. Bunlar, hidrofor sistemleri kompresörler ve buharlaştırıcılar olmak üzere üç ana bileşene sahiptir. Ancak, kondenser ve genleşme valfleri kullanmaya ek olarak, hidrofor sistemleri aynı zamanda yüksek basınç kontrol valfi ve daha yüksek ortam koşullarında gaz soğutucu olarak çalışan özel bir kondenser kullanır.</p>

<p>Geleneksel sistemlerden bir diğer önemli farkı, hidrofor sistemlerinin düşük ve orta sıcaklıklı kompresörler arasında hareket eden aynı soğutucu ile çalışmasıdır. Düşük sıcaklıklı kompresörler orta sıcaklıklı kompresörlerin emişine boşalır. Başka bir deyişle, düşük sıcaklıklı kompresörler, orta sıcaklıktaki kompresörler için bir güçlendirici görevi görür.</p>

<p>Düşük sıcaklıklı vitrin ve dondurucu buharlaştırıcılardan gelen emme gazı, düşük sıcaklıklı subkritik kompresörlere yaklaşık 200 psig'de, CO&#x2082; için kritik noktanın çok altına girer. Düşük sıcaklıklı kompresörler bu gazı yaklaşık 425 psig'de boşaltır, daha sonra orta sıcaklıktaki transkritik kompresörlere girmeden önce orta sıcaklıktaki vitrinlerden ve orta soğutucu buharlaştırıcılarından gelen orta sıcaklık emme gazı ile birleştirir. Orta sıcaklıkta deşarj gazı, ortam koşullarına bağlı olarak, kritik noktanın üzerinde 560 psig'den 1450 psig'e kadar her yerde kompresörleri terk eder. Basıncın 1055 psig'in üzerine çıktığı daha sıcak koşullar altında, sistem transkritik aralıkta çalışır. Bununla birlikte, tüm koşullar altında, orta sıcaklıktaki kompresörlerden çıkan deşarj gazı, taşıdığı ısının dış ortama reddedildiği kondensere beslenir. Bu ısı reddetme işlemi herhangi bir soğutma sisteminde standarttır.</p>

<p>Kompresörlerin, sistemin düşük sıcaklık ve orta sıcaklık aşamalarında boyutlandırılması, kısmi yük işletimi sırasında optimum kapasite kontrolünü sağlamak için dikkatle belirlenir. Kondenser tasarımı, sistem transkritik aralıkta çalışırken yüksek ortam sıcaklıklarında bile yüksek performans için optimize edilmiştir.</p>

<p>Kondenseri terk eden CO&#x2082;, CO&#x2082;'yi flaş tankı adı verilen bir ara basınç alıcısına genişleten bir yüksek basınçlı kontrol valfına beslenir. Gaz, ortam koşullarına bağlı olarak valfe 560 ila 1450 psig girer ve 540 psig'de valftan çıkar. Valf, sistemin en verimli çalışma performansı için gaz soğutucusundan optimum basıncı korumak amacıyla bir tutma valfi gibi çalışacak şekilde tasarlanmıştır.</p>
<p>Flaş deposu (veya daha büyük sistemlerde bulunan tanklar) prensip olarak geleneksel bir alıcı ile aynı şekilde çalışır ve pompa kapanma ve kapatma sırasında genellikle tüm soğutucu şarjını içerebilir. Bununla birlikte, bazı sistemler, daha küçük bir flaş tankı kullanmak için pompalama sırasında gaz soğutucusunda kapasiteden yararlanır. Depodaki basınç, sistem genelinde diferansiyel basıncı korumak için yeterli olan sabit bir seviyede tutulur.</p>
<p>Flaş tankından hem sıvı hem de gaz soğutucu sisteme geri beslenir. Sıvı soğutucu akışkan, geleneksel elektronik genleşme valfleri tarafından kontrol edilen orta ve düşük sıcaklıktaki evaporatörlere verilir. Flaş deposundan gelen buhar, bir flaş gazı baypas valfı vasıtasıyla orta sıcaklıktaki kompresörlere geri beslenir. Flaş gazı baypas valfi, flaş deposunda sabit bir basıncı korur.</p>
<p>Yeni tarif edilen bileşenlerden bazıları dışında, sistem diğer DX sistemlerine benzer şekilde çalışır. Temel farklar sistemin iki aşamalı tasarımı ve sistemdeki tüm evaporatörlerin aynı kaynaktan sıvı ile beslenmesi ile ilgilidir. Çoğu deneyimli teknisyen için, sistem aşırı derecede karmaşık görünmeyecektir.</p>


<h2>Başlıca Bileşenler</h2>

<h2>CO&#x2082; Kompresörleri</h2>
<p>Diğer tüm DX sistemleri gibi, kompresörler de soğutucuyu hidrofor sistemlerinde taşımak için kullanılır. Bununla birlikte, diğer birçok DX sisteminin aksine, hidrofor sistemleri iki grup kompresör kullanır - biri orta sıcaklık kademesi için, diğeri düşük sıcaklık kademesi için. Yarı hermetik, pistonlu kompresörler tipik olarak her iki kademe için kullanılır. Scroll kompresörleri, sistemleri tercih eden müşteriler için düşük sıcaklık aşaması için kullanılabilir. Her iki tip kompresör de çeşitli üreticilerden temin edilebilir.</p>
<p>Kompresörler, bu uygulamada kullanım için özel olarak seçilen poliolester (POE) veya polialkilen glikol (PAG) yağlarıyla çalışır, bu nedenle bir yağ yönetim sistemi gereklidir. Yağ yönetim sisteminin bileşenleri geleneksel DX sistemleri ile aynıdır, ancak CO&#x2082; ile ilişkili daha yüksek işletme basınçlarını karşılamak üzere tasarlanmıştır ve yağ seviyesi regülatörleri (elektronik veya mekanik), bir ayırıcı, bir rezervuar ve bir filtre. CO&#x2082; kompresörleri, kompresör çalışmadığında yağı ısıtmak için karter ısıtıcılarına da ihtiyaç duyarlar.Bazı CO&#x2082; kompresörleri, ortam için tahliye vanaları içerir. Kompresörün önündeki gözetleme camı, çalışma sırasında yağ sisteminin görsel olarak izlenmesini sağlar.</p>
<p>Basınç entalpi diyagramında (sonraki sayfa) bakıldığında, orta sıcaklık sıkıştırma döngüsü, gazın kompresörlere girdiği 400 psig'den başlayarak görülebilir. Oradan, ortam koşullarına bağlı olarak gaz, kompresörlerden deşarj olduğu ve kondensere girdiği için 1385 psig'e kadar ulaşabilir. Tabii ki, gazın bu aralığa ulaşması için, ortam koşulları 80 ° F'yi aşmalıdır.</p>

<p>Tipik olarak orta sıcaklıklı kompresörlerden daha küçüktür (uygulamaya bağlı olarak boyutları değişebilir), ancak düşük sıcaklıklı kompresörler, bir kaskad sistemindeki CO&#x2082; kompresörleri ile aynı şekilde kritik noktanın çok altında çalışır. Bu kompresörler gibi, hidrofor sistemindeki olanlar da düşük sıcaklıktaki evaporatörlerden emme gazı alır. Hidrofor sistemlerinde, emme gazı kompresörlere 200 psig'de girer ve tahliye gazı bunları 400 psig'de bırakır. Oradan, orta sıcaklıklı kompresörlerin emme gazı olmak için orta sıcaklıktaki buharlaştırıcılardan gelen gaz ile birleştirilir.</p>


<p>Sistemin temel bir özelliği, daha iyi kapasite kontrolü için kurşun orta sıcaklık kompresöründe ve daha da fazla enerji tasarrufu için düşük sıcaklıklı kompresörlerde değişken frekanslı bir sürücünün kullanılmasıdır.</p>

<h2>Yağ ayırıcı</h2>
<p>Orta sıcaklık boşaltma gazı orta sıcaklık kompresörlerinden ayrıldığında, bir yağ ayırıcıdan geçer. Yağ separatörleri DX sistemlerinde yaygın bir bileşendir. Güçlendirme sistemleri, kaskad sistemlerinin alt kısmı gibi, sentetik POE yağı kullanır. Hidrofor sistemleri, yağ sistemindeki kirleticileri gidermek için birleştirme filtreleri kullanan yüksek verimli bir mekanik yağ ayırıcı ile donatılmıştır. Ayırıcı, harici bir yağ haznesi olsun veya olmasın kullanılabilir.</p>


<h2>Kondenser / Gaz Soğutucu</h2>
<p>Bu bileşen tipik olarak bir kondansatörün geleneksel bir DX sisteminde yaptığı gibi çalışır. 80 ° F'nin altındaki ortam koşullarında, orta sıcaklıkta boşaltma gazı kondansatör / gaz soğutucusuna girer ve ünitenin bobinlerinden geçerken dış havaya ısıyı reddeder. Bir hidrofor sistemi kondansatörü ile geleneksel bir kondansatör arasındaki temel fark, ortam sıcaklığı 80 ° F'nin üzerine çıktığında, hidrofor sisteminin transkritik aralıkta çalışmaya başlamasıdır. Daha sonra sistemden geçen deşarj gazı başka bir durum değişikliğine uğrayamaz, fakat aksi bilindiği gibi süperkritik bir gaz veya sıvı olarak kalır. Bu son nokta önemli bir ayrımdır. Transkritik koşullar altında, boşaltma gazı, gaz soğutucusuna süperkritik bir sıvı olarak girer ve gaz soğutucudan yüksek basınç kontrol valfine (aşağıda) kadar bu şekilde kalır. Normal bir kondansatördeki gibi gazın aşırı soğutulması gerçekleşmez. Bununla birlikte, 80 ° F'nin altında, ünite daha sonra tipik bir DX sistemindeki bir kondenser gibi çalışır.</p>
<p>Aynı şekilde, değişken hızlı tahrikler kullanılarak kompresörlerde verimlilik kazanımları da yapılır, böylece kondenserdeki fanlar da kontrol edilir. Ayrıca, kondenser bakım veya diğer ihtiyaçlar için bir kesme vanası ile donatılmıştır.</p>
<p>Sistemin verimliliğini daha sıcak iklimlere yaymak için adyabatik bir kondansatör kullanılabilir. Bu tip kondenser, sistemin ortam kuru termometre sıcaklığından daha düşük ıslak termometre sıcaklığında yoğuşmaya devam etmesini sağlar.</p>
<p>Adyabatik kondenserler, kondenserin bobinlerine ulaşmadan önce bir dizi su ile nemlendirilmiş ped içinden akarken ortam havasının sıcaklığını düşürmek için buharlaşma işlemini kullanır. Bu soğutucu hava daha sonra kuru serpantinlerden ısıyı emer. Müşteriler daha sıcak iklimlerde bu yaklaşımın aslında daha fazla enerjide çalıştığını bulmuşlardır.</p>

<h2>Yüksek Basınç Kontrol Vanası</h2>
<p>Kondenser gibi, yüksek basınç kontrol vanası iki çalışma modunda çalışır. Bu ünite bir kondenser olarak çalıştığında genellikle kondenserdeki aşırı soğutmayı kontrol eder. Ünitenin gaz soğutucu olarak çalıştığı koşullar altında (80 ° F ortamın üzerinde), vana ünitedeki basıncı kontrol eder.</p>

<h2>Flaş Tankı</h2>
<p>Yüksek basınç kontrol valfinden genleşmiş gaz, 540 psig'de flaş tankına akar. Flaş deposu, geleneksel bir DX sistemindeki bir alıcının işlevini yerine getirir. 1300 psig'de, pompanın düşmesi veya bir kesinti gibi, gerekirse sistemin tüm şarjını tutabilir. Ancak geleneksel alıcılar sadece buharlaştırıcılara sıvı geri gönderir. Diğer yandan, flaş deposu, buharı tanktan orta sıcaklıktaki emme hatlarına gönderen ve orta sıcaklıklı kompresörlere geri dönen orta sıcaklık buharlaştırıcılarından gazı birleştiren bir flaş gaz baypas valfı ile donatılmıştır.</p>


<h2>Diğer bileşenler</h2>

<h3>Borular</h3>
<p>Daha önce belirtildiği gibi CO&#x2082;'nin faydalarından biri, yüksek hacimsel kapasitesidir. Bu, benzer kapasiteye sahip bir HFC sistemi için gerekenden daha küçük çaplı boruların kullanılmasına izin verir. Aslında, daha küçük çaplar, soğutucu akışkan yükünü azalttığı ve daha yüksek basınçları taşıdıkları için sistemin genel avantajlarına katkıda bulunur. Bazı durumlarda inch inç boru (6 x 1 mm) yeterlidir. Bununla birlikte, sıcak gaz defrostlu sistemlerde, 8 x 1 mm ila x inç (10 x 1 mm) boru gereklidir.</p>

<p>Şu anda sahadaki tesisatlar için kondenser / gaz soğutucusuna giden ve giden borular ve dirsekler program 80 karbon veya paslanmaz çelik kullanmıştır. Borular kaynaklıdır ve nemli koşulların veya hava koşullarına maruz kalmanın olabileceği yerlerde astar ve vernik ile kaplanır. Bununla birlikte, tedarikçilerden gelen son gelişmeler, özellikle CO&#x2082; ile kullanılmak üzere tasarlanmış bir tür bakır boru tesisatının mevcudiyetine yol açmıştır. </p>

<h3>Kontrolörler</h3>
<p>Diğer sistem türlerinde olduğu gibi, hidrofor sistemleri için kontrolörler birçok üreticiden temin edilebilir. Hidrofor sistemlerinde kontrolör, sistem transkritik aralıkta çalışırken sistemin performansını en üst düzeye çıkarmak için gaz soğutucusunda optimum basıncı korur. Bu kontrol yöntemi optimum performans katsayısı (COP) sağlar. 0-10 volt sinyalli ısı geri kazanımı da bu yaklaşımla elde edilebilir.</p>
<p>Sistem, dört veya sekiz kompresöre kadar çalışabilen paket kontrolörleri aracılığıyla kompresör kapasite kontrolünü kullanır. Paket kontrolörleri emme basıncı üzerinde çalışır ve herhangi bir soğutma sistemindeki emme gruplarını kontrol etmek için standart bir araçtır. Paket kontrolörleri, bağlantı modelinin seçimine bağlı olarak, aynı veya farklı boyutlardaki tek adımlı kompresörler ile birleştirilmiş iki kompresör için değişken hızları düzenleyebilir.</p>
<p>Ticari soğutmanın karşılaştığı zorlukları bilen çoğu insan için, çevre dostu doğal soğutucu akışkanlara geçmenin gerekçesi iyi bilinmektedir. Mevcut yaklaşımlar arasında başta CO&#x2082; Booster sistemler en büyük avantajları sunar. Bu sistemlerin tasarımı, çoğu zaman geleneksel DX sistemlerinin yaptığı gibi çalışmalarına olanak tanır ve kurulum ve işletmelerini oldukça basit hale getirir. Hidrofor sistemleri sağlayan HFC soğutucu akışkanlarının tamamen ortadan kaldırılması, bu sistem kullanıcılarını şu anda mevcut olan diğer alternatiflerden daha sürdürülebilir bir çalışma temeline doğru yönlendirir.</p>					<div class="clearfix"></div>
