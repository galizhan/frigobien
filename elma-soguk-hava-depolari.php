				<h2>Elma Soğuk Hava Depoları</h2>
				<div class="row">
					<div class="col-md-12">
						<div class="image-container">
							<img class="" src="assets/img/faaliyet-alanlari/elma-soguk-hava-depolari.webp" alt="Frigobien Elma Soğuk Hava Depoları" title="Frigobien Elma Soğuk Hava Depoları" />
