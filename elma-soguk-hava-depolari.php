					<div class="col-md-12">
						<p class='resimyanyazisi'><strong>Elma soğuk hava depoları</strong>, hasat edilen elmalar pazara sevk edilip son tüketiciye ulaştırılana kadar elmaların muhafaza edilmesinde olmazsa olmazdır. Bu depolar adi depolar, soğuk hava depoları veya atmosfer kontrollü soğuk hava depoları olabilir.<br>Elmaların depoda kalma müddeti, meyvenin depolama zamanı, ağacın beslenme durumu, mevsim ve çeşide göre değişir. Bir çok elma çeşidi –1 ile 0°C de ve %85-90 nisbi nemde uzun süre saklanabilir. Ticari depolardaki sıcaklık 0-2°C dir. Elmalar –2°C donarlar. Golden ve Starking ise en uygun 0°C de ve % 90 nisbi nemde, Granny Smith ise +3°C saklanmalıdır.<p>					</div>
					<div class="clearfix"></div>
					<div class="col-md-12">
						
<h2>Elma Hasat</h2>
<p>Elmada hasat zamanının belirlenmesinde dikkat edilmesi gereken ölçüler, tam çiçeklenmeden olgunluğa kadar geçen gün sayısı, meyve etinin sertliği, meyve kabuğunun rengi, meyvenin daldan kopma durumu ve nişasta düzeyidir. Tam zamanında yapılan hasat ve uygun depolama şartlarının sağlanmasıyla Golden Delicious ve Starspur Golden Delicious çeşitleri 5 ay, Starking Delicious ve Starkrimson Delicious çeşitleri 7 ay, Granny Smith çeşidi ise 9 ay süre ile depoda saklanabilir. Genel olarak yazlık elmalar ağaç üzerinde gerek toplama ve gerekse yeme bakımından tam bir olgunluğa ulaştıkları zaman toplanmalıdırlar. Bu durumda yazlık elmalar yüksek kaliteli olurlar. Kışlık elmalarda ağaç olumu ile yeme olumu arasında çeşit özelliklerine de bağlı olarak uzun bir zaman aralığı vardır. Hasat edilen elma hemen yenilecekse yeme olumunu beklemek gerekir. Hemen tüketilmeyecek ve depolanacak ise ağaç olumuna geldikleri (daldan kolayca koptukları) zaman hasat edilebilir.</p>
<h2>Elma Depolama</h2>
<h3>Etilen Üretimi ve Hassasiyeti</h3>
<p>Elma, olgunlaşma sırasında solunumu artan, klimakterik bir meyve olarak sınıflandırılır. Bu artış, karbondioksit ve etilen, solunum ve otokatalitik etilen üretiminin iç konsantrasyonlarındaki artışlarla ilişkilidir. Endojen etilen üretimi çeşitler arasında büyük farklılıklar gösterebilir. Genel olarak, erken hasat çeşitleri yüksek etilen üretim oranlarına sahiptir ve hızlı bir şekilde olgunlaşır, geç hasat eilenler ise düşük etilen üretim oranlarına sahiptir ve yavaşça olgunlaşır. Elmanın olgunlaşması etilene maruz bırakılarak ilerletilir. Etilen sentezine müdehale edilerek etilen üretiminin önlenmesi veya yavaşlatılması, meyve depolanabilirliğinin arttırılması için bir stratejidir. Bu, esas olarak düşük depolama sıcaklıklarının kullanılması ve atmosfer kontrollü depolama teknolojilerinin uygulanmasıyla elde edilir.</p>
<h3>Solunum Oranları</h3>
<p>Genel olarak, erken hasat çeşitleri yüksek solunum oranlarına sahiptir, geç hasat çeşitleri ise düşük solunum oranlarına sahiptir. Meyvenin solunum hızı direkt olarak sıcaklıktan etkilenir ve solunum yolu sıcaklığı 10°C’nin altındaki depolama sıcaklıkları tarafından bastırılır. Depolama için en düşük sıcaklıklar donma noktasının üstünde ve üşüme yaralanmalarının gelişeceği sıcaklıkların üzerinde olmalıdır.</p>
<h3>Soğutma Koşulları</h3>
<p>Elmanın soğuma hızı, kalitenin korunmasını etkilemektedir, ancak önemi, çeşitliliğe, hasat olgunluğuna, meyvenin beslenme durumuna ve depolama tarihine göre değişmektedir. Hasat mevsiminin ilk yarısında olgunlaşan elma çeşitlerinin hızlı bir şekilde soğutulması çok önemlidir (yaz çeşitleri) çünkü hasat mevsiminin ilerleyen kısımlarında olgunlaşanlardan daha hızlı yumuşarlar. Bir çeşitlilikte, elmalar daha sonraki aşamalarda daha erken evrelere göre daha hızlı yumuşama eğilimindedir. Yavaş soğuma etkileri, depolama uzunluğu arttıkça çoğalır. Bu nedenle, meyvelerin hızlı bir şekilde soğutulmasını sağlamak için hasatta kaynakların yetersiz yatırımı, meyvenin pazarlama için asgari sertlik standartlarını karşılayamayacağı depolama süresinin sonlarına kadar belirgin olmayabilir. Örneğin, soğumadan önce 21°C’de 1 günlük bir gecikme, farazi 7-10 günlük bir depolama ömrü kaybıyla sonuçlanır. Meyvenin kaliteli bakımını en üst seviyeye çıkarmak, sadece hasattan hemen sonra ve depolama sırasında değil, aynı zamanda paketleme, nakliye ve perakende teşhir sırasında da sıcaklığa dikkat etmeyi gerektirir. Bu olayların bir araya gelmesi, hasattan tüketiciye olan bağlantıların sürdürülmesinin önemini vurgulayan “soğuk zincir” olarak tanımlanmaktadır.</p>
<h2>Sonuç</h2>
<h3>Optimum Depolama Koşulları</h3>
<p>Elma üreticileri, elmanın hem sıcaklık hem de atmosfer modifikasyonuna önemli ölçüde tepki verdiğini iyi biriler. Hızlı ve ön soğutmaya önem verilmesi ve doğru uygulanması, elma için 3 ila 6 ay depolama ve bazı durumlarda daha uzun süre orta kaliteli ürün sağlayabilir. Bununla birlikte, modern ticari depolar, elmaların uzun süreli depolanması için sıcaklık yönetimini atmosfer kontrolü ile birleştirmektedir. İyi tasarlanmış bir soğuk hava deposu, elmanın ticari depolanması için önerilen koşullar, çeşitliliğe bağlı olarak -1°C ile 4°C ve %90-95 nem oranına dikkat edilmelidir. Pazardaki kalite standartları arttıkça kabul edilebilir depolama süresi kısalmıştır. Ayrıca, depolanan meyve satışı için mevcut dönem azaldıkça kısa vadeli atmosfer kontrollü depolama daha yaygın hale gelmektedir.</p>
<h3>Kontrollü Atmosfer Değerlendirmeleri</h3>
<p>Elmalar, genellikle kontrollü atmosfer koşullarında depolanan ürünlerdir, ancak gaz bileşimi ve depolama sıcaklığı koşulları, çeşitliliğe, hacme ve atmosferin izlenmesi ve kontrol edilmesi için mevcut ekipmana göre farklılıklar gösterir. Etkileşimler oksijen, karbondioksit ve sıcaklık arasında gerçekleşir. Örneğin, düşük depolama sıcaklıkları, düşük oksijen seviyesi hasarına yatkınlığı artırır. Ayrıca çok düşük oksijen seviyeleri kullanıldığında, oksijen hasarını önlemek için karbondioksit seviyeleri azaltılmalıdır.
