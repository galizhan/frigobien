<h2>Soğuk Hava Depoları Sıcaklıklarına Göre Ele Alınırlar;</h2>
<p>Soğuk hava depoları, içinde depolanacak ürünlerin optimum saklama koşullarına uygun sıcaklıklara göre tasarlanırlar. Soğuk hava depoları tek bölümden oluşabileceği gibi, farklı sıcaklık değerlerine sahip farklı bölümlerden de oluşabilir. Böyle bir soğuk hava deposunun farklı bölümlerinde depolama şartlarında farklı sıcaklıklara ihtiyaç duyan ürünler de muhafaza edilebilir.</p>

<h3 id='serin-muhafaza-depolari'>Serin Muhafaza Depoları</h3>
<p>Ürün işleme hatları gibi yaz sıcaklarında eriyebilen ürünlerin rahatlıkla  işlenebilmesi, paketlenebilmesi amacı ile tesis edilen depolar veya ortamlardır. Yine Sanayilerin çeşitli alanlarında kullanılan kimyasal maddelerin muhafaza edilmeleri için de tercih edilir. 60mm kalınlığında poliüretan sandviç panel ile izolasyonu sağlanan depolarda ya da ortamlarda +18°C sıcaklık sağlanır. Bu depolarda sıcaklık kış aylarında +18°C nin altına düşeceğinden çift rejimli cihazlar ile yazın soğutma kışın ise ısıtma işlemi sağlanır. </p>

<h3 id='soguk-muhafaza-depolari'>Soğuk Muhafaza Depoları</h3>
<p>+4°C / 0°C sıcaklığın sağlandığı depoların izolasyonu, 80mm kalınlığında poliüretan sandviç panel ile sağlanır ve soğuk muhafaza depoları olarak adlandırılırlar. En yaygın tercih edilen depolardır. Soğuk muhafazaya ihtiyaç duyabilecek tüm işlenmiş gıdaların muhafazası için tesis edilir. İşlenmiş gıdalarda bozulmaya neden olabilecek bakteri üremesine engel olmak, enzim aktiviteleri ve oksidasyonu düşürmek temel sebep olarak gösterilebilir. Taze süt ve süt ürünleri, et ve işlenmiş et ürünleri ve taze deniz mahsülleri için tercih edilir.</p>

<h3 id='yas-sebze-ve-meyve-depolari'>Yaş Sebze ve Meyve Depoları</h3>
<p>Yaş sebze ve meyve depoları, -10°C / +16°C sıcaklık aralığında, 80mm kalınlığında poliüretan sandviç panel ile izolasyonu sağlanarak tasarlanabilen depolardır. Bu depolarda sıcaklık değerlerinin arasındaki fark her meyve ve sebzenin muhafaza sıcaklarının farklı olmasından kaynaklanır. (<em>Patates: +13°C, Elma: -10°C</em>)<br> Yaşsebze ve meyve depolarında sadece soğutma sisteminin kurulması, muhafaza işleminin gerçekleşmesinde yeterli olmaz. Meyve sebzeler hasat edildikten sonra yaşamsal faaliyetlerini devam ettirdiklerinden dolayı, depodaki sıcaklık faktörünün yanında oksijen ve nispi nem oranı değerlerini de kontrol altına almak gerekir. Sebze ve meyvelerin yaşamsal faaliyetleri ne derece önlenebilirse, muhafaza süreleri de o derece uzatılabilir. Her meyve ve sebzenin muhafazasında uygulanacak işlemlerin farklılık göstermesi de bu başlığımıza istinaden farklı faaliyet alanlarını doğurmaktadır. <a href='faaliyet-alanlari.php'>Faaliyet alanları</a> bölümünden hepsini inceleyebilirsiniz.</p>

<h3 id='donuk-muhafaza-depolari'>Donuk Ürün Muhafaza Depoları</h3>
<p>Donuk ürün muhafaza depoları, şoklama yöntemi ile kısa sürede çekirdek sıcaklıklarının -18°C'ye düşürülmüş ürünlerin muhafaza edildiği -15°C ile -25°C arası sıcaklıkta çalıştırılan soğuk hava depolarına denir. 120mm ya da 150mm kalınlığında poliüretan sandviç panel ile izolasyonu sağlanan donmuş muhafaza depolarında saklanacak ürünlerin saklama süreleri ürün niteliğine göre ve depolama süresine göre değişmektedir. Depolanan ürünlerin depolama sıcaklığı dikkate alınarak sıcaklığı ayarlanan depolarda saklanan ürünler, saklanma süreleri boyunca bozulma riski olmadan uzun süre muhafaza edilmiş olacaktır. Daha ayrıntılı bilgi almak isterseniz faaliyet alanlarımız bölümünde yer alan <a href='donuk-urun-muhafaza-depolari.php'>Donuk Ürün Muhafaza Depoları</a> sayfamızı inceleyebilirsiniz.</p>

<h3 id='soklama-depolari'>Şoklama Depoları</h3>
