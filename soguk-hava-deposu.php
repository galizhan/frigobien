						<p class="resimyanyazisi"><strong>Soğuk Hava Deposu</strong>, genellikle hayvansal veya tarımsal gıdaların uygun sıcaklık koşullarında uzun süre saklanabilmesi için yalıtımın sağlanması ile izole edilerek inşaa edilen depodur. Genellikle poliüretan sandviç paneller ile izolasyonu sağlanırken, hermetik ya da yarı hermetik soğutma cihazları sayesinde depo içine monte edilen evaparotörler tarafından soğutulur. Depo sıcaklığının düşürülmesi, soğutma cihazının kapasitesine bağlıdır. Ülkemizde birçok faaliyet alanı için kullanılan soğuk hava depoları, yalıtımları paslanmaz paneller ile sağlanarak sağlık sektöründe de ilaç depolama, aşı depolama gibi alanlarda kullanılır.</p></p>
